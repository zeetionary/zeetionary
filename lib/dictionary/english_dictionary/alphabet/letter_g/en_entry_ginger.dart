class EnglishEntryginger extends StatefulWidget {
  const EnglishEntryginger({super.key});

  @override
  State<EnglishEntryginger> createState() => _EnglishEntrygingerState();
}

class _EnglishEntrygingerState extends State<EnglishEntryginger> {
  @override
  void initState() {
    super.initState();
    flutterTts = FlutterTts();
    flutterTts.setLanguage("en-GB");
    flutterTts.setLanguage("en-US");
    fetchSentences();
  }

  FlutterTts flutterTts = FlutterTts();

  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Noun: ginger (derived forms: gingers)
1. Pungent rhizome of the common ginger plant; used fresh as a seasoning especially in Asian cookery (= gingerroot)
 
2. Perennial plant having leafy reedlike stems and branching aromatic rhizomes used as a hot spice
 
3. Dried ground gingerroot (= powdered ginger)
 
4. Liveliness and energy (= pep [informal], peppiness)
"this tonic is guaranteed to give you more ginger";
 
5. [informal] Someone who has red hair (= redhead, redheader, red-header, carrottop [informal], blue [Austral, NZ, informal])

- Adjective: ginger
1. (used especially of hair or fur) having a bright orange-brown colour (= gingery, ginger-haired)
"a ginger kitten";

- Verb: ginger (derived forms: gingered, gingering, gingers)
1. (cooking) add ginger to in order to add flavour
"ginger the soup"
""",
  );

  final String keyword = "ginger";
  List<Map<String, dynamic>> filteredSentences = [];

  Future<void> fetchSentences() async {
    final sentences =
        await DatabaseUtils.instance.fetchFilteredSentences(keyword: keyword);
    setState(() {
      filteredSentences = sentences;
    });
  }

  void speakEnglish(String text, {String? languageCode}) async {
    await flutterTts.setLanguage(languageCode ?? "en-GB");
    await flutterTts.speak(text);
  }

  Future<void> speakheadword(String languageCode) async {
    await flutterTts.setLanguage(languageCode);
    await flutterTts.setPitch(ttsPitch);
    await flutterTts.setSpeechRate(ttsSpeechRate);
    await flutterTts.speak("""ginger""");
  }

  Future<void> speaksentence1(String languageCode) async {
    await flutterTts.setLanguage(languageCode);

    await flutterTts
        .speak("""Make sure you chop the ginger into tiny pieces.""");
  }

  Future<void> speaksentence2(String languageCode) async {
    await flutterTts.setLanguage(languageCode);
    await flutterTts.setPitch(ttsPitch);
    await flutterTts.setSpeechRate(ttsSpeechRate);
    await flutterTts.speak("""The cat's fur is a bright ginger.""");
  }

  Future<void> speaksentence3(String languageCode) async {
    await flutterTts.setLanguage(languageCode);

    await flutterTts
        .speak("""The painting featured a ginger sunset over the hills.""");
  }

  Future<void> speaksentence4(String languageCode) async {
    await flutterTts.setLanguage(languageCode);
    await flutterTts.setPitch(ttsPitch);
    await flutterTts.setSpeechRate(ttsSpeechRate);
    await flutterTts.speak("""Hey, ginger, what are you doing?""");
  }

  @override
  Widget build(BuildContext context) {
    return DefaultTabController(
      length: 4,
      child: Scaffold(
        appBar: const ZeetionaryAppbar(),
        body: NestedScrollView(
          headerSliverBuilder: (context, innerBoxIsScrolled) {
            return [
              CustomSliverAppBar(
                flexibleSpace: FlexibleSpaceBar(
                  background: SingleChildScrollView(
                    child: EntryPageColumn(
                      word: """ginger""",
                      // alsoEnglishWord: "also: ginger",
                      britshText: """IpaUK: /ˈdʒɪndʒə(r)/""",
                      americanText: """IpaUS: /ˈdʒɪndʒər/""",
                      onPressedBritish: () => speakheadword("en-GB"),
                      onPressedAmerican: () => speakheadword("en-US"),
                    ),
                  ),
                ),
                bottom: const CustomTabBarNew(
                  tabs: [
                    UkIconForTab(),
                    KurdIconForTab(),
                    SentencesIconForTab(),
                    VideoIconForTab(),
                  ],
                ),
              ),
            ];
          },
          body: TabBarView(
            children: [
              SingleChildScrollView(
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    EnglishButtonTTS(
                      onBritishPressed: (languageCode) =>
                          startSpeaking(languageCode, englishMeaningConst),
                      onAmericanPressed: (languageCode) =>
                          startSpeaking(languageCode, englishMeaningConst),
                      onStopPressed: stopSpeaking,
                    ),
                    englishMeaningConst,
                  ],
                ),
              ),
              SingleChildScrollView(
                child: CustomColumnWidget(
                  children: [
                    const KurdishVocabulary(text: """
کوردی: زەنجەفیل،	جم‌وجۆڵ، چالاکی، تین‌وگوڕ، وزە، چاپکی، گورجی،	ڕەنگی خەنەیی، ڕەنگی قاوەیی پرتەقاڵی یان زەردی کاڵ،	زەنجەفیلی، قاوەیی پرتەقاڵی، خەنەیی،	خاوەن مووی خەنەیی، مووسوور
"""),
                    const DefinitionKurdish(
                        text:
                            """١. (ناو) ڕەگی زەنجەفیل کە لە خواردندا وەک توونی بەکاردێت"""),
                    SentencesRow(
                      englishText:
                          """Make sure you chop the ginger into tiny pieces.""",
                      kurdishText:
                          """دڵنیابەرەوە کە زەنجەفیلەکە ورد دەکەیت بۆ بەشی بچووکتر.""",
                      onPressedBritish: () => speaksentence1("en-GB"),
                      onPressedAmerican: () => speaksentence1("en-US"),
                    ),
                    const DividerDefinition(),
                    const DefinitionKurdish(
                        text:
                            """٢. (ناو) ڕەنگی سوور یان پرتەقاڵی مەیلەو قاوەیی"""),
                    SentencesRow(
                      englishText: """The cat's fur is a bright ginger.""",
                      kurdishText: """تووکی پشیلەکە سوورێکی درەوشاوەیە.""",
                      onPressedBritish: () => speaksentence2("en-GB"),
                      onPressedAmerican: () => speaksentence2("en-US"),
                    ),
                    const DividerDefinition(),
                    const DefinitionKurdish(
                        text:
                            """٣. (ناو) کەسێک کە قژی سوور یان پرتەقاڵی مەیلەو قاوەیی ھەیە"""),
                    SentencesRow(
                      englishText: """Hey, ginger, what are you doing?""",
                      kurdishText: """ھا، قژسوور، چی دەکەیت؟""",
                      onPressedBritish: () => speaksentence4("en-GB"),
                      onPressedAmerican: () => speaksentence4("en-US"),
                    ),
                    const DividerDefinition(),
                    const DefinitionKurdish(
                        text:
                            """٤. (ھاوەڵناو) ڕەنگی پرتەقاڵی مەیلەو قاوەیی ڕۆشن"""),
                    SentencesRow(
                      englishText:
                          """The painting featured a ginger sunset over the hills.""",
                      kurdishText:
                          """تابلۆکە خۆرئاوابوونێکی پرتەقاڵی بەسەر گردەکانەوە تێدا بوو.""",
                      onPressedBritish: () => speaksentence3("en-GB"),
                      onPressedAmerican: () => speaksentence3("en-US"),
                    ),
                  ],
                ),
              ),
              Consumer(
                builder: (context, ref, child) {
                  if (filteredSentences.isEmpty) {
                    return const NoSentencesFromDatabase();
                  } else {
                    return ListView.builder(
                      itemCount: filteredSentences.length,
                      itemBuilder: (context, index) {
                        final sentence = filteredSentences[index];
                        final showDivider = filteredSentences.length > 1 &&
                            index != filteredSentences.length - 1;
                        return CustomSentenceWidget(
                          englishText: sentence['english'].toString(),
                          frenchText: sentence['french'].toString(),
                          keyword: keyword,
                          onPressedBritish: () => speakEnglish(
                            sentence['english'].toString(),
                            languageCode: "en-GB",
                          ),
                          onPressedAmerican: () => speakEnglish(
                            sentence['english'].toString(),
                            languageCode: "en-US",
                          ),
                          showDivider: showDivider,
                        );
                      },
                    );
                  }
                },
              ),
              const YouTubeScroller(
                children: [
                  YoutubeEmbeddedone(),
                  YoutubeEmbeddedtwo(),
                  YoutubeEmbeddedthree(),
                  YoutubeEmbeddedfour(),
                  YoutubeEmbeddedfive(),
                  // YoutubeEmbeddedsix(),
                  // YoutubeEmbeddedseven(),
                  // YoutubeEmbeddedeight(),
                  // YoutubeEmbeddednine(),
                  // YoutubeEmbeddedten(),
                  // YoutubeEmbeddedeleven(),
                  // YoutubeEmbeddedtwelve(),
                  // YoutubeEmbeddedthirteen(),
                  // YoutubeEmbeddeddfourteen(),
                  // YoutubeEmbeddedfifteen(),
                  // YoutubeEmbeddeddsixteen(),
                  // YoutubeEmbeddeddseventeen(),
                  // YoutubeEmbeddeddeighteen(),
                  // YoutubeEmbeddeddnineteen(),
                  // YoutubeEmbeddedtwenty(),
                  // YoutubeEmbeddedmulti(),
                  YoutubeEmbeddedend(),
                ],
              ),
            ],
          ),
        ),
      ),
    );
  }
}

