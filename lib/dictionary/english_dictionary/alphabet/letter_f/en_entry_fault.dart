class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Noun: fault (derived forms: faults)
1. A wrong action attributable to bad judgment, ignorance or inattention (= mistake, error)
"I could understand his English in spite of his grammatical faults";
 
2. An imperfection in an object or machine (= defect, flaw)
"if there are any faults you should send it back to the manufacturer";
 
3. The quality of being inadequate or falling short of perfection (= demerit)
"he knew his own faults much better than she did";
 
4. (geology) a crack in the earth's crust resulting from the displacement of one side with respect to the other (= faulting, geological fault, shift, fracture, break)
"they built it right over a geological fault";
 
5. (electronics) equipment failure attributable to some defect in a circuit (loose connection or insulation failure or short circuit etc.)
"it took much longer to find the fault than to fix it"
 
6. Responsibility for a bad situation or event
"it was John's fault"
 
7. (sport) a serve that is illegal (e.g., that lands outside the prescribed area)
"he served too many double faults"

- Verb: fault (derived forms: faulting, faulted, faults)
1. Put or pin the blame on (= blame)
 
2. Commit a mistake or make an error
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

