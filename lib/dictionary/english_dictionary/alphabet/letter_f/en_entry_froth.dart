    text: """
- Noun: froth (derived forms: froths)
1. A mass of small bubbles formed in or on a liquid (- foam)
"the beer had a thick head of froth";

- Verb: froth (derived forms: frothed, frothing, froths)
1. Become bubbly, frothy or foaming (- foam, fizz, effervesce, sparkle, form bubbles)
"The boiling soup was frothing";

2. Make froth or foam and become bubbly (- spume, suds [N. Amer])
 
3. Exude or expel foam
"the angry man was frothing at the mouth"
""",
  );

  final String keyword = "froth";
  List<Map<String, dynamic>> filteredSentences = [];

  Future<void> fetchSentences() async {
    final sentences =
        await DatabaseUtils.instance.fetchFilteredSentences(keyword: keyword);
    setState(() {
      filteredSentences = sentences;
    });
