                            """٢. (ناو) ڕێڕەوی بینایەک کە لە هەردوو لایەوە ژووڕی تێدایە"""),
                    const AlsoEnglishckb(word: "ھەروەھا: corridor"),
                    SentencesRow(
                      englishText:
                          """I headed for Scott's office down the hall.""",
