                    SingleChildScrollView(
                      child: Column(
                        crossAxisAlignment: CrossAxisAlignment.end,
                        children: [
                          const KurdishVocabulary(text: """
کوردی: لێک‌دان، پێکاکێشان، لێک‌کەوتن، ڕووداو، قەومین، بەڵا(ی کت‌وپڕ)، بوویەر، تووش، ڕێکەوت، ھەڵکەوت
