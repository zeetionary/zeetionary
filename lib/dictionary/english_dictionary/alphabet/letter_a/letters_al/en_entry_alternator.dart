    const String videoIdthree = '1t4-BDHEIZo';
    const double startSecondsthree = 3;
    const String videoIdfour = 'qE-_5drM4CM';
    const double startSecondsfour = 13;
    const String videoIdfive = 'COPftLQiCUY';
    const double startSecondsfive = 1;
    // final String _videoId = 'FuBAKTCCnt4';
    // final double _startSeconds = 4;

    return DefaultTabController(
      length: 4,
      child: Scaffold(
        body: NestedScrollView(
          headerSliverBuilder: (context, innerBoxIsScrolled) {
            return [
              CustomSliverAppBar(
                flexibleSpace: FlexibleSpaceBar(
                  background: SingleChildScrollView(
                    child: EntryPageColumn(
                      word: """alternator""",
                      // alsoEnglishWord: "also: alternator",
                      britshText: """IpaUK: /ˈɔːltəneɪtə(r)/""",
                      americanText: """IpaUS: /ˈɔːltəneɪtər/""",
                      onPressedBritish: () => speakheadword("en-GB"),
                      onPressedAmerican: () => speakheadword("en-US"),
                    ),
                  ),
                ),
                bottom: const CustomTabBarNew(
                  tabs: [
                    UkIconForTab(),
                    KurdIconForTab(),
