    text: """
- Noun: formality (derived forms: formalities)
1. A requirement of etiquette or custom (= formalities)
"a mere formality";
 
2. A manner that strictly observes all forms and ceremonies (= formalness)
"the formality of his voice made the others pay him close attention";
 
3. Compliance with formal rules
"courtroom formality"
""",
  );

  final String keyword = "formality";
  List<Map<String, dynamic>> filteredSentences = [];

  Future<void> fetchSentences() async {
    final sentences =
        await DatabaseUtils.instance.fetchFilteredSentences(keyword: keyword);
    setState(() {
      filteredSentences = sentences;
    });
  }

  void speakEnglish(String text, {String? languageCode}) async {
    await flutterTts.setLanguage(languageCode ?? "en-GB");
    await flutterTts.speak(text);
