    const double startSecondsthree = 60297;
    const String videoIdfour = 'd5E2AQKuCyU';
    const double startSecondsfour = 7126;
    const String videoIdfive = 'ZJ_85q3GBBA';
    const double startSecondsfive = 277;

    return DefaultTabController(
      length: 4,
      child: Scaffold(
        body: NestedScrollView(
          headerSliverBuilder: (context, innerBoxIsScrolled) {
            return [
              CustomSliverAppBar(
                flexibleSpace: FlexibleSpaceBar(
                  background: SingleChildScrollView(
                    child: EntryPageColumn(
                      word: """inconsiderately""",
                      // alsoEnglishWord: "also: inconsiderately",
                      britshText: """IpaUK: /ˌɪnkənˈsɪdərətli/""",
                      americanText: """IpaUS: /ˌɪnkənˈsɪdərətli/""",
                      onPressedBritish: () => speakheadword("en-GB"),
                      onPressedAmerican: () => speakheadword("en-US"),
                    ),
                  ),
                ),
                bottom: const CustomTabBarNew(
                  tabs: [
                    UkIconForTab(),
