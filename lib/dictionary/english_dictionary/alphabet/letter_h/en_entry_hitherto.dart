    const String videoIdtwo = '8X1xYpPmKvA';
    const double startSecondstwo = 4033;
    const String videoIdthree = '_ZQ03ki7-UU';
    const double startSecondsthree = 1060;
    const String videoIdfour = 'ZkIRVNIqBKQ';
    const double startSecondsfour = 741;
    const String videoIdfive = 'tnLXW-p5Wig';
    const double startSecondsfive = 52;

    return DefaultTabController(
      length: 4,
      child: Scaffold(
        body: NestedScrollView(
          headerSliverBuilder: (context, innerBoxIsScrolled) {
            return [
              CustomSliverAppBar(
                flexibleSpace: FlexibleSpaceBar(
                  background: SingleChildScrollView(
                    child: EntryPageColumn(
                      word: """hitherto""",
                      // alsoEnglishWord: "also: hitherto",
                      britshText: """IpaUK: /ˌhɪðəˈtuː/""",
                      americanText: """IpaUS: /ˌhɪðərˈtuː/""",
                      onPressedBritish: () => speakheadword("en-GB"),
                      onPressedAmerican: () => speakheadword("en-US"),
                    ),
                  ),
                ),
                bottom: const CustomTabBarNew(
                  tabs: [
                    UkIconForTab(),
                    KurdIconForTab(),
                    SentencesIconForTab(),
                    VideoIconForTab(),
                  ],
                ),
              ),
            ];
          },
          body: TabBarView(
            children: [
              SingleChildScrollView(
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    EnglishButtonTTS(
                      onBritishPressed: (languageCode) =>
                          startSpeaking(languageCode, englishMeaningConst),
                      onAmericanPressed: (languageCode) =>
                          startSpeaking(languageCode, englishMeaningConst),
                      onStopPressed: stopSpeaking,
                    ),
                    englishMeaningConst,
                  ],
                ),
              ),
              SingleChildScrollView(
                child: CustomColumnWidget(
                  children: [
                    const KurdishVocabulary(text: """
کوردی: تا ئێستا، هەتانووکە
"""),
                    const DefinitionKurdish(
