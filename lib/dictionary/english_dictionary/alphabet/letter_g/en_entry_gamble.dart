                            """٢. (کردار) خۆ خستنە مەترسی ئەوەی شتێک لە دەست بدەیت بۆ ئەوەی سەرکەوتوو بیت"""),
                    SentencesRow(
                      englishText:
                          """He's gambling his reputation on this deal.""",
                      kurdishText:
                          """لەسەر ئەم ڕێکەوتنە ناوبانگی دەخاتە مەترسییەوە.""",
                      onPressedBritish: () => speaksentence3("en-GB"),
                      onPressedAmerican: () => speaksentence3("en-US"),
                    ),
                    const DividerSentences(),
                    SentencesRow(
                      englishText:
                          """It was wrong to gamble with our children's future.""",
                      kurdishText:
                          """ئەوە ھەڵە بوو داھاتووی منداڵەکانمان بخەینە مەترسییەوە.""",
                      onPressedBritish: () => speaksentence4("en-GB"),
                      onPressedAmerican: () => speaksentence4("en-US"),
                    ),
                    const DividerDefinition(),
                    const DefinitionKurdish(
                        text:
                            """٣. (ناو) شتێک کە دەیکەیت و دەزانیت مەترسیدارە بەڵام بەھیوایت سەرکەوتوو بێت"""),
                    SentencesRow(
                      englishText:
                          """It was the biggest gamble of his political career.""",
                      kurdishText: """گەورەترین مەترسیی ژیانی سیاسیی بوو.""",
                      onPressedBritish: () => speaksentence5("en-GB"),
                      onPressedAmerican: () => speaksentence5("en-US"),
                    ),
                    const DividerSentences(),
                    SentencesRow(
                      englishText:
                          """She knew she was taking a gamble but decided it was worth it.""",
                      kurdishText:
                          """دەیزانی خۆی دەخاتە مەترسییەوە بەڵام بڕیاری دا کە شایەنییەتی.""",
                      onPressedBritish: () => speaksentence6("en-GB"),
                      onPressedAmerican: () => speaksentence6("en-US"),
                    ),
                  ],
                ),
              ),
              Consumer(
                builder: (context, ref, child) {
                  if (filteredSentences.isEmpty) {
                    return const NoSentencesFromDatabase();
                  } else {
                    return ListView.builder(
                      itemCount: filteredSentences.length,
                      itemBuilder: (context, index) {
                        final sentence = filteredSentences[index];
                        final showDivider = filteredSentences.length > 1 &&
                            index != filteredSentences.length - 1;
                        return CustomSentenceWidget(
                          englishText: sentence['english'].toString(),
                          frenchText: sentence['french'].toString(),
                          keyword: keyword,
                          onPressedBritish: () => speakEnglish(
                            sentence['english'].toString(),
                            languageCode: "en-GB",
                          ),
                          onPressedAmerican: () => speakEnglish(
                            sentence['english'].toString(),
                            languageCode: "en-US",
                          ),
                          showDivider: showDivider,
                        );
                      },
                    );
                  }
                },
              ),
              const YouTubeScroller(
                children: [
                  YoutubeEmbeddedone(),
                  YoutubeEmbeddedtwo(),
                  YoutubeEmbeddedthree(),
                  YoutubeEmbeddedfour(),
                  YoutubeEmbeddedfive(),
                  // YoutubeEmbeddedsix(),
