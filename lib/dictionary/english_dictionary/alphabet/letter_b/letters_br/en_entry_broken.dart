class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const DividerDefinition(),
          // Speaker icon for British English
          Row(
            children: [
              CustomIconButtonBritish(
                onPressed: () => startSpeaking("en-GB"),
              ),
              CustomIconButtonAmerican(
                onPressed: () => startSpeaking("en-US"),
              ),
              // Conditional rendering of pause button
              if (isSpeaking)
                IconButton(
                  icon: const Icon(Icons.pause, size: 30),
                  onPressed: () {
                    stopSpeaking();
                  },
                ),
            ],
          ),
          // Speaker icon for American English
          const EnglishMeaningConst(
            text: """
- Adjective: broken
1. Not in working order; not functioning (= busted, bust [Brit, informal], up the spout [informal], knackered [Brit, informal], out of order, on the blink [informal], on the fritz [N. Amer, informal], kaput [informal], rooted [Austral, NZ, informal])
"a broken washing machine"; "the coke machine is broken"; 
 
2. Physically and forcibly separated into pieces, cracked or split
"a broken mirror"; "a broken tooth"; "a broken leg"; "his neck is broken"
 
3. Subdued or brought low in condition or status (= crushed, humbled, humiliated, low)
"a broken man"; "his broken spirit";

4. Tamed or trained to obey (= broken in)
"a horse broken to the saddle"; "this old nag is well broken in";
 
5. Topographically very uneven (= rugged)
"broken terrain";

6. Imperfectly spoken or written
"broken English"
 
7. Thrown into a state of disarray or confusion (= confused, disordered, upset)
"troops fleeing in broken ranks";

8. Weakened and infirm
"broken health resulting from alcoholism"
 
9. Destroyed financially (= wiped out, impoverished)
"the broken fortunes of the family";

10. (meteorology) discontinuous
"broken clouds"; "broken sunshine"
 
11. Lacking a part or parts
"a broken set of encyclopaedia"
 
12. Not continuous in space, time,, sequence or varying abruptly
"broken lines of defence"; "a broken cable transmission"; "broken sleep"; "tear off the stub above the broken line"; "a broken note"; "broken sobs"
 
13. (especially of promises or contracts) having been violated or disregarded
"broken promises"; "broken contracts";
""",
          ),
        ],
      ),
    );
  }

  // Function to start TTS
  Future<void> startSpeaking(String languageCode) async {
    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak("""
ZZZZZZZZZZZZZZZZZZZZZZZZZZ
""");

    // Update the state to reflect that TTS is in progress
    setState(() {
      isSpeaking = true;
    });
  }

  // Function to stop TTS
  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    // Update the state to reflect that TTS is stopped
    setState(() {
      isSpeaking = false;
    });
  }
}

