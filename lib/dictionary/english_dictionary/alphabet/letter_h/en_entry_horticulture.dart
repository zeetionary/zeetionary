    const double startSecondstwo = 48;
    const String videoIdthree = 'pQJmn6z3h5Q';
    const double startSecondsthree = 896;
    const String videoIdfour = 'dTrFW-4gM4I';
    const double startSecondsfour = 116;
    const String videoIdfive = 'ZHanNbB2OaI';
    const double startSecondsfive = 418;

    return DefaultTabController(
      length: 4,
      child: Scaffold(
        body: NestedScrollView(
          headerSliverBuilder: (context, innerBoxIsScrolled) {
            return [
              CustomSliverAppBar(
                flexibleSpace: FlexibleSpaceBar(
                  background: SingleChildScrollView(
                    child: EntryPageColumn(
                      word: """horticulture""",
                      // alsoEnglishWord: "also: horticulture",
