- Verb: imitate (Derived forms: imitated, imitating, imitates)
1. Reproduce someone's behaviour or looks (- copy, simulate)
"The mime imitated the passers-by";
 
2. Appear like, as in behaviour or appearance
"Life imitate art"
 
3. Make a reproduction or copy of
""",
  );

  final String keyword = "imitate";
  List<Map<String, dynamic>> filteredSentences = [];

