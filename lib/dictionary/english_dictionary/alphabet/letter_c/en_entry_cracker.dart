class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

// Function to stop TTS
  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    // Update the state to reflect that TTS is stopped
    setState(() {
      isSpeaking = false;
    });
  }

// Create an instance of EnglishMeaningConst with the desired text
  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Noun: cracker (derived forms: crackers)
1. A thin crisp wafer made of flour and water with or without leavening and shortening; unsweetened or semisweet
 
2. [US, informal] A poor White person in the southern United States (= redneck [N. Amer, informal])
 
3. A programmer who cracks (gains unauthorized access to) computers, typically to do malicious things
"crackers are often mistakenly called hackers"
 
4. Firework consisting of a small explosive charge and fuse in a heavy paper casing (= firecracker, banger [Brit, informal])
 
5. A party favour consisting of a paper roll (usually containing candy or a small favour) that pops when pulled at both ends (= snapper, cracker bonbon)
 
6. [Brit, informal] An outstanding example of its kind (= beauty, beaut [informal], knockout [informal], lulu [informal], killer [informal], blinder [Brit, informal])
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

