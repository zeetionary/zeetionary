    const double startSecondstwo = 913;
    const String videoIdthree = '9_ie0fO4-CM';
    const double startSecondsthree = 156;
    const String videoIdfour = 'GplzznHXO04';
    const double startSecondsfour = 200;
    const String videoIdfive = 'QLFYJAURgHI';
    const double startSecondsfive = 507;

    return DefaultTabController(
      length: 4,
      child: Scaffold(
        body: NestedScrollView(
          headerSliverBuilder: (context, innerBoxIsScrolled) {
            return [
              CustomSliverAppBar(
                flexibleSpace: FlexibleSpaceBar(
                  background: SingleChildScrollView(
                    child: EntryPageColumn(
                      word: """hoody""",
                      // alsoEnglishWord: "also: hoody",
