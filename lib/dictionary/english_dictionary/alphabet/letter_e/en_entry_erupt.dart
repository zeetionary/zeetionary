class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Verb: erupt (derived forms: erupted, erupting, erupts)
1. Start abruptly (= break out)
"After 1989, peace erupted in the former East Bloc";
 
2. Erupt or intensify suddenly (= irrupt, flare up, flare, break open, burst out)
"Unrest erupted in the country";
 
3. Start to burn or burst into flames (= ignite, catch fire, take fire, combust, conflagrate)
"Marsh gases erupted suddenly";
 
4. Break out (= come out, break through, push through)
"The tooth erupted and had to be extracted";
 
5. Become active and spew forth lava and rocks (= belch, extravasate)
"Vesuvius erupts once in a while";
 
6. Force out or release suddenly and often violently something pent up (= break, burst)
"erupt in anger";
 
7. Appear on the skin
"A rash erupted on her arms after she had touched the exotic plant"
 
8. Become raw or open (= recrudesce, break out)
"Such boils tend to erupt";
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

