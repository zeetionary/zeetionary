                      englishText:
                          """He enjoys the hurly-burly of political debate.""",
                      kurdishText: """حەزی بە جەنجاڵی دیبەیتە سیاسییەکەیە.""",
                      onPressedBritish: () => speaksentence1("en-GB"),
                      onPressedAmerican: () => speaksentence1("en-US"),
                    ),
