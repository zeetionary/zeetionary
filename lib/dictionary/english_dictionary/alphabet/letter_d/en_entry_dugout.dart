class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

// Function to stop TTS
  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    // Update the state to reflect that TTS is stopped
    setState(() {
      isSpeaking = false;
    });
  }

// Create an instance of EnglishMeaningConst with the desired text
  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Noun: dugout (derived forms: dugouts)
1. Either of two low shelters on either side of a baseball diamond where the players and coaches sit during the game
 
2. A fortification of earth; mostly or entirely below ground (= bunker)
 
3. A shelter for humans or domesticated animals and livestock based on a hole or depression dug into the ground; can be fully recessed into the earth, with a flat roof covered by ground, semi-recessed, with a constructed wood or sod roof standing out, or dug into a hillside. (= pithouse, pit-house, pit house)
"Dugouts are one of the most ancient types of human housing known to archeologists, and the same methods have evolved into modern "earth shelter" technology.";
 
4. A canoe made by hollowing out and shaping a large log (= dugout canoe, pirogue, piragua)
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

