enum TtsState { playing }

class EnglishEntryappal extends StatefulWidget {
  const EnglishEntryappal({super.key});

  @override
  State<EnglishEntryappal> createState() => _EnglishEntryappalState();
}

class _EnglishEntryappalState extends State<EnglishEntryappal> {
  @override
  void initState() {
    super.initState();
    flutterTts = FlutterTts();
    flutterTts.setLanguage("en-GB");
    flutterTts.setLanguage("en-US");
    fetchSentences();
  }

  FlutterTts flutterTts = FlutterTts();

  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.setPitch(ttsPitch);
    await flutterTts.setSpeechRate(ttsSpeechRate);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Verb: appal (derived forms: appalling, appals)
Usage: Brit, Cdn (US: appall)
1. Strike with disgust or revulsion (= shock, offend, scandalize, scandalise [Brit], appall [US], outrage)
"The scandalous behaviour of this married woman appalled her friends";
 
2. Fill with apprehension or alarm; cause to be unpleasantly surprised (= dismay, alarm, appall [US], horrify)
"I was appalled at the thought of being late for my interview";
""",
  );
// 188888880002200

  final String keyword = "appal";
  List<Map<String, dynamic>> filteredSentences = [];

  Future<void> fetchSentences() async {
    final sentences =
        await DatabaseUtils.instance.fetchFilteredSentences(keyword: keyword);
    setState(() {
      filteredSentences = sentences;
    });
  }

  void speakEnglish(String text, {String? languageCode}) async {
    await flutterTts.setLanguage(languageCode ?? "en-GB");
    await flutterTts.speak(text);
  }

  Future<void> speakheadword(String languageCode) async {
    await flutterTts.setLanguage(languageCode);
    await flutterTts.setPitch(ttsPitch);
    await flutterTts.setSpeechRate(ttsSpeechRate);
    await flutterTts.speak("""appal""");
  }

  Future<void> speakappal737(String languageCode) async {
    await flutterTts.setLanguage(languageCode);
    await flutterTts.setPitch(ttsPitch);
    await flutterTts.setSpeechRate(ttsSpeechRate);
    await flutterTts.speak("The idea of sharing a room appalled her.");
  }

  @override
  Widget build(BuildContext context) {
    const String videoIdend = 'UIimvo3NAbM';
    const double startSecondsend = 78;
    const String videoIdone = 'K2rY7JkwL2k';
    const double startSecondsone = 479;
    const String videoIdtwo = 'aBohGITX4SQ';
    const double startSecondstwo = 21;
    const String videoIdthree = 'C_LHPOesjnM';
    const double startSecondsthree = 189;
    const String videoIdfour = 'ftQlDnqkw5c';
    const double startSecondsfour = 36;
    const String videoIdfive = 'u5ZraSSCgjQ';
    const double startSecondsfive = 180;
    // final String _videoId = '4HljsKwpv3g';
