class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Adjective: equal
1. Having the same quantity, value, or measure as another
"on equal terms"; "all men are equal before the law"
 
2. Having the requisite qualities or resources to meet a task (= adequate)
"he was equal to the task";

Verb: equal (derived forms: equalled, equaling, equaled, equalling, equals)
1. Be identical or equivalent to (= be)
"One dollar equals 1,000 rubles these days!";
 
2. Be equal to in quality or ability (= touch, rival, match)
"Your performance doesn't even equal that of your colleagues";
 
3. Make equal, uniform, corresponding, or matching (= match, equalize, equalise [Brit], equate)

- Noun: equal (derived forms: equals)
1. A person who is of equal standing with another in a group (= peer, match, compeer)
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const DividerDefinition(),
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

