class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Noun: farm (Derived forms: farms)
1. Workplace consisting of farm buildings and cultivated land as a unit
"it takes several people to work the farm"
 
2. An area of land or water used to cultivate specific crops, raise livestock or fish, or hold or produce other commercial products
"wind farm"; "salmon farm"
 
3. (computing) a group of computers used for some coordinated purposes
"a server farm"

- Verb: farm (Derived forms: farming, farms, farmed)
1. Be a farmer; work as a farmer
"My son is farming in California"
 
2. (farming) cultivate by growing, often involving improvements by means of agricultural techniques (= grow, raise, produce)
"We farm wheat here";
 
3. Collect fees or profits
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const DividerDefinition(),
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

