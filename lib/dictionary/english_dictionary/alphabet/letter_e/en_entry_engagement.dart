class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Noun: engagement (derived forms: engagements)
1. A hostile meeting of opposing military forces in the course of a war (= battle, conflict, fight)
"he lost his romantic ideas about war when he got into a real engagement";
 
2. A meeting arranged in advance (= date, appointment)
 
3. A mutual promise to marry (= betrothal, troth [archaic], betrothment)
 
4. The act of giving someone a job (= employment)
 
5. Employment for performers or performing groups that lasts for a limited period of time (= booking)
"the play had engagements throughout the summer";
 
6. Contact by fitting together (= mesh, meshing, interlocking)
"the engagement of the clutch";
 
7. The act of sharing in the activities of a group (= participation, involvement, involution)
"the teacher tried to increase his students' engagement in class activities";
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const DividerDefinition(),
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

