class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  // Function to stop TTS
  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Verb: backfire (derived forms: backfired, backfires, backfiring)
1. Come back to the originator of an action with an undesired effect (= backlash, recoil)
"Your comments may backfire and cause you a lot of trouble";
 
2. Emit a loud noise as a result of undergoing a backfire
"My old car backfires all the time"
 
3. Set a controlled fire to halt an advancing forest to prairie fire

- Noun: backfire
1. The backward escape of gases and unburned gunpowder after a gun is fired (= blowback)
 
2. A loud noise made by the explosion of fuel in the manifold or exhaust of an internal combustion engine
 
3. A fire that is set intentionally in order to slow an approaching forest fire or grassfire by clearing a burned area in its path
 
4. A miscalculation that recoils on its maker (= boomerang)
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

