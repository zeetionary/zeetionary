class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Noun: fatigue (derived forms: fatigues)
1. Temporary loss of strength and energy resulting from hard physical or mental work (= weariness, tiredness)
"he was hospitalized for extreme fatigue"; "growing fatigue was apparent from the decline in the execution of their athletic skills";
 
2. Used of materials (especially metals) in a weakened state caused by long stress
"metal fatigue"

3. (always used with a modifier) boredom resulting from overexposure to something
"he was suffering from museum fatigue"; "after watching TV with her husband she had a bad case of football fatigue"; "the American public is experiencing scandal fatigue"; "political fatigue"
 
4. (military) labour of a nonmilitary kind done by soldiers (cleaning, digging, draining or so on) (= fatigue duty)
"they were assigned to kitchen fatigues"; "the soldiers were put on fatigue to teach them a lesson";

- Verb: fatigue (derived forms: fatiguing, fatigues, fatigued)
1. Lose interest or become bored with something or somebody (= tire, pall, weary, jade)
"I'm so fatigued of your mother and her complaints about my food";
 
2. Exhaust or get tired through overuse or great strain or stress (= tire, wear upon, tire out, wear, weary, jade, wear out, outwear, wear down, fag out [Brit, informal], fag [informal], knacker [Brit, informal])
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

