2. A perforated or corrugated metal plate used in a storage battery as a conductor and support for the active material (= storage-battery grid)
 
3. An electrode placed between the cathode and anode of a vacuum tube to control the flow of electrons through the tube (= control grid)
 
4. A cooking utensil of parallel metal bars; used to grill fish or meat (= gridiron, grill)
 
5. (cartography) a method of marking off maps into areas

- Verb: grid (derived forms: grided, griding, grids)
1. Mark with or put into a grid
""",
