class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

// Function to stop TTS
  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Noun: delivery (derived forms: deliveries)
1. The act of delivering or distributing something (as goods or mail) (= bringing)
"his reluctant delivery of bad news";
 
2. The event of giving birth
"she had a difficult delivery"
 
3. Your characteristic style or manner of expressing yourself orally (= manner of speaking, speech)
"his delivery was quite abrupt";
 
4. The voluntary transfer of something (title or possession) from one party to another (= livery, legal transfer)
 
5. (baseball) the act of throwing a baseball or softball by the pitcher towards home plate, which initiates play by giving the batter a chance to hit it (= pitch)
 
6. Recovery or preservation from loss or danger (= rescue, deliverance, saving)
"work is the delivery of mankind";
 
7. The act of delivering a child (= obstetrical delivery)
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

