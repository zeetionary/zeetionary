class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Verb: enter (derived forms: enters, entered, entering)
1. To come or go into (= come in, get into, get in, go into, go in, move into)
"the boat entered an area of shallow marshes";
 
2. Become a participant; be involved in (= participate)
"enter a race"; "enter an agreement"; "enter a drug treatment program"; "enter negotiations";
 
3. Register formally as a participant or member (= enroll [N. Amer], inscribe, enrol [Brit, Cdn], recruit)
 
4. Be or play a part of or in (= figure)
"Elections enter prominently in every government program";
 
5. Make a record of; set down in permanent form (= record, put down)
 
6. (dramaturgy) come on stage
 
7. Take on duties or office (= accede)
"enter to the throne";
 
8. Place onto or put into something (= insert, infix, introduce)
"enter a picture into the text";
 
9. Set out on (an enterprise or subject of study) (= embark)
"she entered upon a new career";

- Noun: enter (derived forms: enters)
1. The key on a computer keyboard that starts a new line when writing text; in other contexts the key usually causes a form or window to perform a default function, e.g. accept the data that has just been input (= return key, return, enter key)
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const DividerDefinition(),
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

