- Adjective: infatuated
1. Feeling strong, obsessive or foolish love (- enamored [US], in love, potty [Brit, informal], smitten, soft on, taken with, enamoured [Brit, Cdn], besotted, lovestruck [informal])
"he was infatuated with her";
""",
  );

