                    ),
                    englishMeaningConst,
                  ],
                ),
              ),
              SingleChildScrollView(
                child: CustomColumnWidget(
                  children: [
                    const KurdishVocabulary(text: """
کوردی: گەرچی، ھەرچەن کە، لەگەڵ ئەوەش، سەرەڕای ئەوەش
