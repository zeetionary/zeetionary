                            """٣. (ناو) بینا یان ژوورێکی گەورە بۆ چاوپێکەوتنی گشتی، نانخواردن، کۆنسێرت، هتد"""),
                    const DividerSentences(),
                    SentencesRow(
                      englishText:
                          """There are three dining halls on campus.""",
                      kurdishText: """سێ هۆڵی نانخواردن هەیە لە کەمپەکە.""",
