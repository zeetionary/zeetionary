                    const AlsoEnglishckb(word: "ھەروەھا: moan"),
                    SentencesRow(
                      englishText: """He lay on the floor groaning.""",
                      kurdishText: """لەسەر زەوییەکە کەوت نووزەی دەھات.""",
                      onPressedBritish: () => speaksentence1("en-GB"),
