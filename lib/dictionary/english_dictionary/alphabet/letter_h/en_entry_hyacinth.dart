                            """١. (ناو) ڕووەکێک کە تۆپەڵێک گوڵی شین، سپی، یان پەمەیی کە بۆنی خۆشە و گوڵەکان لەسەر قەدێکی ئەستوور پێکەوە گەشە دەکەن"""),
                    SentencesRow(
                      englishText:
