class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

// Function to stop TTS
  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Adjective: elaborate
1. Marked by complexity and richness of detail (= luxuriant)
"an elaborate lace pattern";
 
2. Developed or executed with care and in minute detail (= detailed, elaborated)
"the elaborate register of the inhabitants prevented tax evasion";

- Verb: elaborate (derived forms: elaborated, elaborating, elaborates)
1. Describe and explain in detail (= lucubrate [archaic], expatiate, exposit, enlarge, flesh out, expand, expound, dilate)
"She elaborated on the main ideas in her dissertation";
 
2. Produce from basic elements or sources; change into a more developed product
"The bee elaborates honey"
 
3. Make more complex, intricate, or rich (= complicate, refine, rarify [rare], rarefy)
"elaborate a design or pattern";
 
4. Work out in detail (= work out)
"elaborate a plan";
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

