1. Having a feeling of home; cosy and comfortable (- homelike, homey, homy, mumsy [Brit, informal], gemütlich)
"the homely everyday atmosphere";
 
2. Plain and unpretentious
"homely truths"; "letters to his son full of homely advice"; "homely fare"
 
3. Without artificial refinement or elegance
"plain homely furniture"; "homely manners"
 
4. [N. Amer] Lacking in physical beauty or proportion (- plain, unhandsome)
"a homely child"; "several of the buildings were downright homely";
""",
