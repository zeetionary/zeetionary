                    SentencesIconForTab(),
                    VideoIconForTab(),
                  ],
                ),
              ),
            ];
          },
          body: TabBarView(
            children: [
              SingleChildScrollView(
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    EnglishButtonTTS(
                      onBritishPressed: (languageCode) =>
                          startSpeaking(languageCode, englishMeaningConst),
                      onAmericanPressed: (languageCode) =>
                          startSpeaking(languageCode, englishMeaningConst),
                      onStopPressed: stopSpeaking,
                    ),
                    englishMeaningConst,
                  ],
                ),
              ),
              SingleChildScrollView(
                child: CustomColumnWidget(
                  children: [
                    const KurdishVocabulary(text: """
کوردی: ناتەواوی، عەیب، خەوش،	(شووشە، چینکۆ و ھتد) موو، چڕژ، چڕژاوی، قەڵەشێوی، درز،	خاڵی لاوازی، کەم‌وکووڕی، ناتەواوی، شکستی، عەیب، ئیراد،	ھەڵە، لێ‌ھەڵقڵیشان، خەتا
"""),
// With short examples define "flaw", please follow LX instructions
                    const DefinitionKurdish(
                        text:
                            """١. (ناو) ھەڵەی شتێک کە واتای ئەوەیە ھەڵەی تێدایە یان باش کار ناکات"""),
                    SentencesRow(
                      englishText:
                          """The report reveals fatal flaws in security at the airport.""",
                      kurdishText:
                          """ڕاپۆرتەکە خەوشی کوشندە لە سەلامەتیی فڕۆکەخانەکە پیشان دەدات.""",
                      onPressedBritish: () => speaksentence1("en-GB"),
                      onPressedAmerican: () => speaksentence1("en-US"),
                    ),
