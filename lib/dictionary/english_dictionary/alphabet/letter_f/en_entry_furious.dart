    text: """
- Adjective: furious
1. Marked by extreme and violent energy (- ferocious, fierce, savage)
"a furious battle";
 
2. Marked by extreme anger (- angered, enraged, infuriated, maddened, ropable [Austral, NZ], ropeable [Austral, NZ])
"furious about the accident"; "a furious scowl";
 
3. (of the elements) as if showing violent anger (- angry, raging, tempestuous, wild)
"furious winds";
 
4. Feeling or showing extreme anger (- irate, ireful, steaming [informal], fuming, seething, hopping mad [informal], apoplectic [informal], livid)
"wilful stupidity makes him absolutely furious";
""",
  );

