    const String videoIdtwo = 'sfsE7w1zZKY';
    const double startSecondstwo = 180;
    const String videoIdthree = 'hi9Rf0oLdHk';
    const double startSecondsthree = 3614;
    const String videoIdfour = 'MzRucw88wrY';
    const double startSecondsfour = 585;
    const String videoIdfive = 'zC3DLxwGzDk';
    const double startSecondsfive = 811;

    return DefaultTabController(
      length: 4,
      child: Scaffold(
        body: NestedScrollView(
          headerSliverBuilder: (context, innerBoxIsScrolled) {
            return [
              CustomSliverAppBar(
                flexibleSpace: FlexibleSpaceBar(
                  background: SingleChildScrollView(
                    child: EntryPageColumn(
                      word: """hoodwink""",
                      // alsoEnglishWord: "also: hoodwink",
                      britshText: """IpaUK: /ˈhʊdwɪŋk/""",
                      americanText: """IpaUS: /ˈhʊdwɪŋk/""",
                      onPressedBritish: () => speakheadword("en-GB"),
                      onPressedAmerican: () => speakheadword("en-US"),
                    ),
                  ),
                ),
