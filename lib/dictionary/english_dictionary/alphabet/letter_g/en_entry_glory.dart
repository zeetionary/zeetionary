"the glory of the sunrise";
 
3. An indication of radiant light drawn around the head of a saint (= aura, aureole, halo, nimbus, gloriole, aureola)

- Verb: glory (derived forms: glories, glorying, gloried)
1. Rejoice proudly
""",
  );

