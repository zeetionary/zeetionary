    const String videoIdfour = 'CiRBQ3frG6U';
    const double startSecondsfour = 641;
    const String videoIdfive = 'S6rDvbQ-mz4';
    const double startSecondsfive = 233;

    return DefaultTabController(
      length: 4,
      child: Scaffold(
        body: NestedScrollView(
          headerSliverBuilder: (context, innerBoxIsScrolled) {
            return [
              CustomSliverAppBar(
                flexibleSpace: FlexibleSpaceBar(
                  background: SingleChildScrollView(
                    child: EntryPageColumn(
                      word: """incarceration""",
                      // alsoEnglishWord: "also: incarceration",
                      britshText: """IpaUK: /ɪnˌkɑːsəˈreɪʃn/""",
                      americanText: """IpaUS: /ɪnˌkɑːrsəˈreɪʃn/""",
                      onPressedBritish: () => speakheadword("en-GB"),
                      onPressedAmerican: () => speakheadword("en-US"),
                    ),
                  ),
                ),
                bottom: const CustomTabBarNew(
                  tabs: [
                    UkIconForTab(),
