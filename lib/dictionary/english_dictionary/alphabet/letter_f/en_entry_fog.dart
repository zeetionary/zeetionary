                      kurdishText:
                          """شارۆچکەکە بە چینێکی ئەستوور لە تەم داپۆشرابوو.""",
                      onPressedBritish: () => speaksentence3("en-GB"),
                      onPressedAmerican: () => speaksentence3("en-US"),
                    ),
                    const DividerSentences(),
                    SentencesRow(
                      englishText:
                          """The fog finally lifted (= disappeared).""",
                      kurdishText: """تەمەکە لەکۆتاییدا نەما.""",
                      onPressedBritish: () => speaksentence4("en-GB"),
                      onPressedAmerican: () => speaksentence4("en-US"),
                    ),
                    const DividerSentences(),
                    SentencesRow(
                      englishText: """We drove slowly through the fog.""",
                      kurdishText: """بە ھێواشی بە ناو تەمەکەدا لێمانخوڕی.""",
