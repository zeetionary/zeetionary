                      americanText: """IpaUS: /ˈfɑːləʊər/""",
                      onPressedBritish: () => speakheadword("en-GB"),
                      onPressedAmerican: () => speakheadword("en-US"),
                    ),
                  ),
                ),
                bottom: const TabBar(
                  tabs: [
                    UkIconForTab(),
                    KurdIconForTab(),
                    SentencesIconForTab(),
                    VideoIconForTab(),
                  ],
                ),
              ),
            ];
          },
          body: TabBarView(
            children: [
              SingleChildScrollView(
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    const DividerDefinition(),
                    EnglishButtonTTS(
                      onBritishPressed: (languageCode) =>
                          startSpeaking(languageCode, englishMeaningConst),
                      onAmericanPressed: (languageCode) =>
                          startSpeaking(languageCode, englishMeaningConst),
                      onStopPressed: stopSpeaking,
                    ),
                    englishMeaningConst,
                  ],
                ),
              ),
              SingleChildScrollView(
                child: CustomColumnWidget(
                  children: [
                    const DividerDefinition(),
                    const KurdishVocabulary(text: """
کوردی: مرید، شاگرد، دواکەوتوو، شوێن‌کەوتوو، تەک‌کەوتوو، پەیڕەو، دواڕەو، لایەنگر، پشتیوان
"""),
// With short examples define "follower", please follow LX instructions
                    const DefinitionKurdish(
                        text:
                            """١. (ناو) کەسێک کە شوێن کەسێک یان باوەڕێک دەکەوێت"""),
                    SentencesRow(
                      englishText: """She still has many loyal followers.""",
                      kurdishText: """ھێشتا زۆر شوێنکەوتووی وەفاداری ھەیە.""",
                      onPressedBritish: () => speaksentence1("en-GB"),
                      onPressedAmerican: () => speaksentence1("en-US"),
                    ),
                    const DividerSentences(),
                    SentencesRow(
                      englishText:
                          """He is often referred to as a follower of Darwin.""",
                      kurdishText:
                          """زۆرجار وەک شوێنکەوتووی داروین دادەنرێت.""",
                      onPressedBritish: () => speaksentence2("en-GB"),
                      onPressedAmerican: () => speaksentence2("en-US"),
                    ),
                    const DividerDefinition(),
                    const DefinitionKurdish(
                        text:
                            """٢. (ناو) کەسێک کە حەزی بە چالاکییەکی دیاریکراو ھەیە و ئاگای لە ھەموو ھەواڵێک ھەیە لەسەری"""),
                    SentencesRow(
                      englishText:
                          """This book is only for the dedicated follower of modernist architecture.""",
                      kurdishText:
                          """ئەم کتێبە تەنھا بۆ شوێنکەوتووانی خۆبەستەی تەلارسازیی سەردەمە.""",
                      onPressedBritish: () => speaksentence3("en-GB"),
                      onPressedAmerican: () => speaksentence3("en-US"),
                    ),
                    const DividerDefinition(),
                    const DefinitionKurdish(
                        text:
                            """٣. (ناو) کەسێک کە وەک کەسێکی دیکە کار دەکات"""),
                    SentencesRow(
                      englishText: """She is a leader, not a follower.""",
                      kurdishText: """سەرکردەیە نەک شوێنکەوتوو.""",
                      onPressedBritish: () => speaksentence4("en-GB"),
                      onPressedAmerican: () => speaksentence4("en-US"),
                    ),
                    const DividerDefinition(),
                    const DefinitionKurdish(
                        text:
                            """٤. (ناو) کەسێک کە لە تۆڕێکی کۆمەڵایەتی چاودێری کارەکانی کەسێک دەکات"""),
                    SentencesRow(
                      englishText:
                          """She has over 100,000 followers on Instagram.""",
                      kurdishText:
                          """سەروو ١٠٠٠٠٠ شوێنکەتووی لە ئینستاگرام ھەیە.""",
                      onPressedBritish: () => speaksentence5("en-GB"),
                      onPressedAmerican: () => speaksentence5("en-US"),
                    ),
                  ],
                ),
              ),
              Consumer(
                builder: (context, ref, child) {
                  if (filteredSentences.isEmpty) {
                    return const NoSentencesFromDatabase();
                  } else {
                    return ListView.builder(
                      itemCount: filteredSentences.length,
                      itemBuilder: (context, index) {
                        final sentence = filteredSentences[index];
                        final showDivider = filteredSentences.length > 1 &&
                            index != filteredSentences.length - 1;
                        return CustomSentenceWidget(
                          englishText: sentence['english'].toString(),
                          frenchText: sentence['french'].toString(),
                          keyword: keyword,
                          onPressedBritish: () => speakEnglish(
                            sentence['english'].toString(),
                            languageCode: "en-GB",
                          ),
                          onPressedAmerican: () => speakEnglish(
                            sentence['english'].toString(),
                            languageCode: "en-US",
                          ),
                          showDivider: showDivider,
                        );
                      },
                    );
                  }
                },
              ),
              const YouTubeScroller(
                children: [
                  YoutubeEmbeddedone(),
                  YoutubeEmbeddedtwo(),
                  YoutubeEmbeddedthree(),
                  YoutubeEmbeddedfour(),
                  YoutubeEmbeddedfive(),
                  // YoutubeEmbeddedsix(),
