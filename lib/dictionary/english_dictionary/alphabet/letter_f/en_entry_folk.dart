2. A social division of (usually preliterate) people (= tribe)
 
3. People descended from a common ancestor; blood relations (= family, family line, kinfolk, kinsfolk, phratry, kith and kin)
"his folk has lived in Massachusetts since the Mayflower";
 
4. The traditional and typically anonymous music that is an expression of the life of people in a community (= folk music, ethnic music)
""",
  );

