    const String videoIdtwo = 'HhFOqNJWp4Q';
    const double startSecondstwo = 6091;
    const String videoIdthree = 'RIY4f369VLA';
    const double startSecondsthree = 495;
    const String videoIdfour = 'su6zJeRQLUQ';
    const double startSecondsfour = 1218;
    const String videoIdfive = '_wGBEaAZEBc';
    const double startSecondsfive = 1072;

    return DefaultTabController(
      length: 4,
      child: Scaffold(
        body: NestedScrollView(
          headerSliverBuilder: (context, innerBoxIsScrolled) {
            return [
              CustomSliverAppBar(
                flexibleSpace: FlexibleSpaceBar(
                  background: SingleChildScrollView(
                    child: EntryPageColumn(
                      word: """ill-treatment""",
                      // alsoEnglishWord: "also: ill-treatment",
                      britshText: """IpaUK: /ˌɪl ˈtriːtmənt/""",
                      americanText: """IpaUS: /ˌɪl ˈtriːtmənt/""",
                      onPressedBritish: () => speakheadword("en-GB"),
                      onPressedAmerican: () => speakheadword("en-US"),
                    ),
                  ),
                ),
                bottom: const CustomTabBarNew(
                  tabs: [
                    UkIconForTab(),
                    KurdIconForTab(),
                    SentencesIconForTab(),
                    VideoIconForTab(),
                  ],
                ),
              ),
            ];
          },
          body: TabBarView(
            children: [
              SingleChildScrollView(
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    EnglishButtonTTS(
                      onBritishPressed: (languageCode) =>
                          startSpeaking(languageCode, englishMeaningConst),
                      onAmericanPressed: (languageCode) =>
                          startSpeaking(languageCode, englishMeaningConst),
                      onStopPressed: stopSpeaking,
                    ),
                    englishMeaningConst,
                  ],
                ),
              ),
              SingleChildScrollView(
                child: CustomColumnWidget(
                  children: [
//                     const KurdishVocabulary(text: """
