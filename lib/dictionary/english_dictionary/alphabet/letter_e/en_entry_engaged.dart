class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Adjective: engaged
1. Having one's attention, mind or energy occupied (= occupied)
"deeply engaged in conversation";
 
2. Pledged to be married (= bespoken, betrothed)
"the engaged couple";
 
3. Involved in military hostilities
"the desperately engaged ships continued the fight"
 
4. Reserved in advance (= booked, set-aside)
"All highways must be five lanes wide and either three or four of these lanes must be engaged for passenger cars only";
 
5. (of facilities such as telephones or lavatories) unavailable for use by anyone else or indicating unavailability (= busy, in use)
"receptionists' telephones are always engaged";
 
6. (used of toothed parts or gears) interlocked and interacting (= meshed, intermeshed)
"the gears are engaged";
 
7. Having services contracted for
"the carpenter engaged (or employed) for the job is sick"
 
8. (architecture) built against or attached to a wall
"engaged columns"
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

