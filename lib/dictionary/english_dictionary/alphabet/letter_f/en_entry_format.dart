- Noun: format (derived forms: formats)
1. The general appearance, lay out or arrangement of something
 
2. The organization of information according to preset specifications (usually for computer processing) (= formatting, data format, data formatting)

- Verb: format (derived forms: formatting, formatted, formats)
1. Put or arrange a document into a specific format (= arrange)
"Format this letter so it can be printed out";
 
2. Determine the arrangement of (data) for storage and display (in computer science)
 
3. Divide (a disk) into marked sectors so that it may store data (= initialize, initialise [Brit])
"Please format this disk before entering data!";
""",
  );

  final String keyword = "format";
  List<Map<String, dynamic>> filteredSentences = [];

  Future<void> fetchSentences() async {
    final sentences =
        await DatabaseUtils.instance.fetchFilteredSentences(keyword: keyword);
    setState(() {
      filteredSentences = sentences;
    });
