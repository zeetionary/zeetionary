                    SentencesRow(
                      englishText:
                          """Digital imaging is used in photography and graphic design.""",
                      kurdishText:
                          """وێنەگرتنی دیجیتاڵ لە فۆتۆگرافی و گرافیک دیزایندا بەکاردێت.""",
                      onPressedBritish: () => speaksentence1("en-GB"),
                      onPressedAmerican: () => speaksentence1("en-US"),
                    ),
                  ],
                ),
