                      onPressedAmerican: () => speaksentence2("en-US"),
                    ),
                    const DividerDefinition(),
                    const DefinitionKurdish(
                        text:
                            """٢. (ناو) شوێن یان دۆخێکی مەترسیدار، بە تایبەتی یەکێک کە زۆر سەختە تێیدا متمانە بە کەس بکەیت یان سەرکەوتوو بیت"""),
                    const DividerSentences(),
                    SentencesRow(
                      englishText:
                          """It's a jungle out there—you've got to be strong to succeed.""",
                      kurdishText:
                          """دوونیایەکی سەختە ئەوێ ـ دەبێت بەهێز بیت بۆ سەرکەوتن.""",
