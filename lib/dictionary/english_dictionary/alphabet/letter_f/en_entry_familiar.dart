class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Adjective: familiar
1. Well known or easily recognized
"a familiar figure"; "familiar songs"; "familiar guests"
 
2. (usually followed by 'with') well informed about or knowing thoroughly (= conversant)
"familiar with the complex machinery"; "he was familiar with those roads";
 
3. Having mutual interests or affections; of established friendship (= intimate)
"on familiar terms";
 
4. Within normal everyday experience; common and ordinary; not strange
"familiar ordinary objects found in every home"; "a familiar everyday scene"; "a familiar excuse"; "a day like any other filled with familiar duties and experiences"

- Noun: familiar (Derived forms: familiars)
1. A person attached to the household of a high official (as a pope or bishop) who renders service in return for support
 
2. A friend who is frequently in the company of another (= companion, comrade, fellow, associate, yokefellow [N. Amer])
 
3. A spirit (usually in animal form) that acts as an assistant to a witch or wizard (= familiar spirit)
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const DividerDefinition(),
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

