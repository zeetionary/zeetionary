class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Verb: extract (derived forms: extracts, extracted, extracting)
1. Remove, usually with some force or effort; also used in an abstract sense (= pull out, pull, pull up, take out, draw out, rip out, tear out)
"extract a bad tooth";
 
2. Get despite difficulties or obstacles
"I extracted a promise from the Dean for two new positions"
 
3. Deduce (a principle) or construe (a meaning) (= educe [formal], evoke, elicit, draw out)
"We extracted some interesting linguistic data from the native informant";
 
4. (chemistry) purify or isolate using distillation (= distill [N. Amer], distil [Brit])
"extract the essence of this compound";
 
5. (mining) separate (a metal) from an ore
 
6. Obtain from a substance, as by mechanical action (= press out, express)
"Italians extract coffee rather than filter it";
 
7. Take out of a literary work in order to cite or copy (= excerpt, take out)
 
8. (mathematics) calculate the root of a number

- Noun: extract (derived forms: extracts)
1. A solution obtained by steeping or soaking a substance (usually in water) (= infusion)
 
2. A passage selected from a larger work (= excerpt, excerption, selection)
"he presented extracts from William James' philosophical writings";
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

