  FlutterTts flutterTts = FlutterTts();

  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.setPitch(ttsPitch);
    await flutterTts.setSpeechRate(ttsSpeechRate);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Verb: gulp (Derived forms: gulping, gulped, gulps)
1. To swallow hurriedly, greedily or in one draught (- quaff, swig, neck [Brit, informal], slug [informal])
"The men gulped down their beers";
 
2. Utter or make a noise, as when swallowing too quickly
"He gulped for help after choking on a big piece of meat"

- Noun: gulp (Derived forms: gulps)
1. A large and hurried swallow (- draft [N. Amer], draught [Brit, Cdn], swig)
"he finished it at a single gulp";
 
2. A spasmodic reflex of the throat made as if in swallowing (- gulping)
 
3. A large mouthful of drink (- slug, draught [Brit], draft [US])
""",
  );

  final String keyword = "gulp";
