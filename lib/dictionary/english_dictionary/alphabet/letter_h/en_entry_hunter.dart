    const String videoIdtwo = 'AwhBTrzzqeg';
    const double startSecondstwo = 181;
    const String videoIdthree = 'IVUjJpk0qWc';
    const double startSecondsthree = 47;
    const String videoIdfour = 'fhpAIPFNYag';
    const double startSecondsfour = 0;
    const String videoIdfive = 'PbemQhFSid8';
    const double startSecondsfive = 4;

    return DefaultTabController(
      length: 4,
      child: Scaffold(
        body: NestedScrollView(
          headerSliverBuilder: (context, innerBoxIsScrolled) {
            return [
              CustomSliverAppBar(
                flexibleSpace: FlexibleSpaceBar(
                  background: SingleChildScrollView(
                    child: EntryPageColumn(
                      word: """hunter""",
                      // alsoEnglishWord: "also: hunter",
                      britshText: """IpaUK: /ˈhʌntə(r)/""",
                      americanText: """IpaUS: /ˈhʌntər/""",
                      onPressedBritish: () => speakheadword("en-GB"),
                      onPressedAmerican: () => speakheadword("en-US"),
                    ),
                  ),
                ),
                bottom: const CustomTabBarNew(
                  tabs: [
                    UkIconForTab(),
                    KurdIconForTab(),
                    SentencesIconForTab(),
                    VideoIconForTab(),
                  ],
                ),
              ),
            ];
          },
          body: TabBarView(
            children: [
              SingleChildScrollView(
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    EnglishButtonTTS(
                      onBritishPressed: (languageCode) =>
                          startSpeaking(languageCode, englishMeaningConst),
                      onAmericanPressed: (languageCode) =>
                          startSpeaking(languageCode, englishMeaningConst),
                      onStopPressed: stopSpeaking,
                    ),
                    englishMeaningConst,
                  ],
                ),
              ),
              SingleChildScrollView(
                child: CustomColumnWidget(
                  children: [
                    const KurdishVocabulary(text: """
کوردی: ڕاوچی، نێچیرڤان، شکاروان، شکارچی
