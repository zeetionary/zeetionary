                      onPressedAmerican: () => speaksentence1("en-US"),
                    ),
                    const DividerSentences(),
                    SentencesRow(
                      englishText:
                          """The hounds had lost the scent of the fox near the river.""",
                      kurdishText:
                          """سەگە ڕاوییەکان بۆنی ڕێوییەکەیان ونکرد نزیک لە ڕووبارەکە.""",
