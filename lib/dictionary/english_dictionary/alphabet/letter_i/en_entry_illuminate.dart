1. Introduce light into (- light, illume, illumine, light up)
 
2. Make free from confusion or ambiguity; make clear (- clear, clear up, shed light on, crystallize, crystallise [Brit], crystalize, crystalise [Brit], straighten out, sort out, enlighten, elucidate)
 
3. (art) add embellishments and paintings to (medieval manuscripts)
""",
  );

