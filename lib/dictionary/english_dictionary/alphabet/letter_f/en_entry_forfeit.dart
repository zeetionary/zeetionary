  FlutterTts flutterTts = FlutterTts();

  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Verb: forfeit (derived forms: forfeits, forfeiting, forfeited)
1. Lose (something) or lose the right to (something) by some error, offence, or crime (= give up, throw overboard, waive, forgo, forego)
"you've forfeited your right to name your successor"; "forfeited property";

- Noun: forfeit (derived forms: forfeits)
1. Something that is lost or surrendered as a penalty (= forfeiture)
 
2. A penalty for a fault or mistake that involves losing or giving up something (= forfeiture)
"the contract specified forfeits if the work was not completed on time";
 
3. The act of losing or surrendering something as a penalty for a mistake or fault or failure to perform etc. (= forfeiture, sacrifice)

- Adjective: forfeit
1. Surrendered as a penalty (= confiscate, forfeited)
""",
  );

  final String keyword = "forfeit";
  List<Map<String, dynamic>> filteredSentences = [];

  Future<void> fetchSentences() async {
    final sentences =
        await DatabaseUtils.instance.fetchFilteredSentences(keyword: keyword);
    setState(() {
      filteredSentences = sentences;
    });
  }

  void speakEnglish(String text, {String? languageCode}) async {
    await flutterTts.setLanguage(languageCode ?? "en-GB");
    await flutterTts.speak(text);
  }

  Future<void> speakheadword(String languageCode) async {
    await flutterTts.setLanguage(languageCode);
    await flutterTts.setPitch(ttsPitch);
    await flutterTts.setSpeechRate(ttsSpeechRate);
    await flutterTts.speak("""forfeit""");
  }

  Future<void> speaksentence1(String languageCode) async {
    await flutterTts.setLanguage(languageCode);

    await flutterTts
        .speak("""He has forfeited his right to be taken seriously.""");
  }

  Future<void> speaksentence2(String languageCode) async {
    await flutterTts.setLanguage(languageCode);

    await flutterTts
        .speak("""If you cancel your flight, you will forfeit your deposit.""");
  }

  Future<void> speaksentence3(String languageCode) async {
    await flutterTts.setLanguage(languageCode);
    await flutterTts.setPitch(ttsPitch);
    await flutterTts.setSpeechRate(ttsSpeechRate);
    await flutterTts.speak(
        """The team might have avoided the forfeits if it had handled the situation differently.""");
  }

  Future<void> speaksentence4(String languageCode) async {
    await flutterTts.setLanguage(languageCode);
    await flutterTts.setPitch(ttsPitch);
    await flutterTts.setSpeechRate(ttsSpeechRate);
    await flutterTts.speak(
        """This booking fee will be forfeit should you decide not to take up the accommodation.""");
  }

