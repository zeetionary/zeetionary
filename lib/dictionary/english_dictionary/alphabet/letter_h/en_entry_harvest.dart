1. Gather, as of natural products (- reap, glean [archaic])
"harvest the grapes";
 
2. Remove from a culture or a living or dead body, as for the purposes of transplantation
"Should one harvest organs from dead people for transplants?"

- Noun: harvest (Derived forms: harvests)
1. The yield from plants in a single growing season (- crop)
 
2. The consequence of an effort or activity
"they gathered a harvest of examples"; "a harvest of love"
 
3. The gathering of a ripened crop (- harvesting, harvest home)
 
4. The season for gathering crops (- harvest time)
""",
  );

