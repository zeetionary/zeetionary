1. Being one more than four (= 5, v)

- Noun: five (derived forms: fives)
1. The cardinal number that is the sum of four and one (= 5, V, cinque, quint, quintet, fivesome, quintuplet, pentad, Phoebe, Little Phoebe)
 
2. A team that plays basketball (= basketball team)
 
3. A playing card or a domino or a die whose upward face shows five pips (= five-spot [US])
""",
  );

