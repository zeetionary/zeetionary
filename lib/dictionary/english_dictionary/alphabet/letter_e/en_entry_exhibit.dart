class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Verb: exhibit (derived forms: exhibited, exhibiting, exhibits)
1. Show an attribute, property, knowledge, or skill
"he exhibits a great talent"
 
2. To show, make visible or apparent (= expose, display)
"The Metropolitan Museum is exhibiting Goya's works this month";
 
3. Give an exhibition of to an interested audience (= show, demo, present, demonstrate, showcase)
"She exhibits her dogs frequently";
 
4. Walk ostentatiously (= parade, march)
"She exhibits her new husband around town";

- Noun: exhibit (derived forms: exhibits)
1. An object or statement produced before a court of law and referred to while giving evidence
 
2. Something shown to the public (= display, showing)
"the museum had many exhibits of oriental art";
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const DividerDefinition(),
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

