"industrial output"
 
2. Employed in industry
"the industrial classes"; "industrial work"
 
3. Suitable to stand up to hard wear
"industrial carpeting"
 
4. Having highly developed industries
"the industrial revolution"; "an industrial nation"
""",
  );

