    const String videoIdone = 'YvZPU-KBCiQ';
    const double startSecondsone = 798;
    const String videoIdtwo = '67cWZ8c2Tbw';
    const double startSecondstwo = 412;
    const String videoIdthree = 'BD9AarmQctI';
    const double startSecondsthree = 260;
    const String videoIdfour = 'lBj7vTUUpYM';
    const double startSecondsfour = 761;
    const String videoIdfive = 'ylgq8TX-xWE';
    const double startSecondsfive = 693;

    return DefaultTabController(
      length: 4,
      child: Scaffold(
        body: NestedScrollView(
          headerSliverBuilder: (context, innerBoxIsScrolled) {
            return [
              CustomSliverAppBar(
                flexibleSpace: FlexibleSpaceBar(
                  background: SingleChildScrollView(
                    child: EntryPageColumn(
                      word: """immortalize""",
                      // alsoEnglishWord: "also: immortalize",
                      britshText: """IpaUK: /ɪˈmɔːtəlaɪz/""",
                      americanText: """IpaUS: /ɪˈmɔːrtəlaɪz/""",
                      onPressedBritish: () => speakheadword("en-GB"),
                      onPressedAmerican: () => speakheadword("en-US"),
                    ),
                  ),
                ),
                bottom: const CustomTabBarNew(
                  tabs: [
                    UkIconForTab(),
                    KurdIconForTab(),
                    SentencesIconForTab(),
                    VideoIconForTab(),
                  ],
                ),
              ),
            ];
          },
          body: TabBarView(
            children: [
              SingleChildScrollView(
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    EnglishButtonTTS(
                      onBritishPressed: (languageCode) =>
                          startSpeaking(languageCode, englishMeaningConst),
                      onAmericanPressed: (languageCode) =>
                          startSpeaking(languageCode, englishMeaningConst),
                      onStopPressed: stopSpeaking,
                    ),
                    englishMeaningConst,
                  ],
                ),
              ),
              SingleChildScrollView(
                child: CustomColumnWidget(
                  children: [
//                     const KurdishVocabulary(text: """
