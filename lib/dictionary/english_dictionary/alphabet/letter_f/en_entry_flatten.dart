2. Become flat or flatter (= flatten out, pancake)
"The landscape flattened";
 
3. (music) lower the pitch of (musical notes) (= drop)
 
4. Destroy (a building or buildings) so as to make nearly level the ground (= level, raze, rase, dismantle, tear down, take down, pull down)
""",
