    text: """
- Noun: frequency (derived forms: frequencies)
1. The number of occurrences within a given time period (= frequence, oftenness)
"the frequency of modulation was 40 cycles per second";
 
2. The ratio of the number of observations in a statistical category to the total number of observations (= relative frequency)
 
3. The number of observations in a given statistical category (= absolute frequency)
""",
  );

