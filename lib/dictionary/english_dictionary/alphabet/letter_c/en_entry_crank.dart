class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    // Extract text from EnglishMeaningConst and store it in textToSpeak
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    // Update the state to reflect that TTS is in progress
    setState(() {
      isSpeaking = true;
    });
  }

// Function to stop TTS
  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    // Update the state to reflect that TTS is stopped
    setState(() {
      isSpeaking = false;
    });
  }

// Create an instance of EnglishMeaningConst with the desired text
  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Noun: crank (Derived forms: cranks)
1. A bad-tempered person (= grouch, grump, churl, crosspatch)
 
2. A whimsically eccentric person (= crackpot [informal], nut [informal], nutcase [informal], fruitcake [informal], screwball [informal])
 
3. [informal] An amphetamine derivative (trade name Methedrine) used in the form of a crystalline hydrochloride; used as a stimulant to the nervous system and as an appetite suppressant (= methamphetamine, methamphetamine hydrochloride, Methedrine, meth [informal], deoxyephedrine, chalk [informal], glass [informal], ice [informal], shabu [Asia, informal], trash [informal], gak [informal], crystal meth)
 
4. A hand tool consisting of a rotating shaft with parallel handle (= starter)

- Adjective: crank 
1. (used of boats) inclined to heel over easily under sail (= cranky [informal], tender, tippy)

- Verb: crank (Derived forms: cranks, cranking, cranked)
1. Travel along a zigzag path (= zigzag)
"The river cranks through the countryside";
 
2. Start by cranking (= crank up)
"crank up the engine";
 
3. Rotate with a crank (= crank up)
 
4. Fasten with a crank
 
5. Bend into the shape of a crank
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const DividerDefinition(),
          // Using the EnglishButtonTTS class
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          // Speaker icon for American English
          englishMeaningConst,
        ],
      ),
    );
  }
}

