                      onPressedAmerican: () => speaksentence1("en-US"),
                    ),
                    const DividerSentences(),
                    SentencesRow(
                      englishText:
                          """She stroked the soft fur on the dog's back.""",
                      kurdishText:
                          """دەستی دەھێنا بە فەرووە نەرمەکەی سەر پشتی سەگەکەدا.""",
                      onPressedBritish: () => speaksentence2("en-GB"),
                      onPressedAmerican: () => speaksentence2("en-US"),
                    ),
                    const DividerSentences(),
                    SentencesRow(
                      englishText:
                          """Native Americans traded furs with early European settlers.""",
                      kurdishText:
                          """ئەمریکییە ڕەسەنەکان فەروویان دەگۆڕییەوە لەگەڵ نیشتەجێ ئەورووپییەکان.""",
