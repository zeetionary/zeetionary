"a hearty meal";
 
3. Endowed with or exhibiting great bodily or mental health (- full-blooded, lusty, red-blooded)
"a hearty glow of health";
 
4. Consuming abundantly and with gusto
"a hearty (or healthy) appetite"
 
5. Deeply felt
"hearty support"

- Noun: hearty (Derived forms: hearties)
Usage: Brit
1. A sporty unrefined person
 
2. [Brit, archaic] Comrade, companion, good fellow; a supposed term of familiar address between sailors
""",
