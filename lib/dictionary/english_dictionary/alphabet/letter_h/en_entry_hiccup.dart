                    englishMeaningConst,
                  ],
                ),
              ),
              SingleChildScrollView(
                child: CustomColumnWidget(
                  children: [
                    const KurdishVocabulary(text: """
کوردی: نزگرە، ھەرس، ھەسر، شھی، ھکەھک، ئیسک،	کێشەی نیوەچڵ، ھەڵوەستەی کاتی یان کورت، ئاستەنگی چکۆلانە، کێشەی بچووک، مەسەلەیەکی ھیچ، گرفتێکی چکۆلانە
