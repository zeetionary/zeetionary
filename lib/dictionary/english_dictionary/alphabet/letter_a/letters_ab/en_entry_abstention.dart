                          SentencesRow(
                            englishText:
                                "The Church insisted on abstention from all luxuries.",
                            kurdishText:
                                "کڵێساکە پێداگیری دەکرد لە خۆبەدوورگرتن لە ھەموو جۆرە شتێکی جوان.",
