    text: """
- Noun: headline (Derived forms: headlines)
1. The heading or caption of a newspaper article (- newspaper headline)

- Verb: headline (Derived forms: headlined, headlines, headlining)
1. Publicize widely or highly, as if with a headline
 
2. (publication) provide (a newspaper page or a story) with a headline
""",
  );

  final String keyword = "headline";
  List<Map<String, dynamic>> filteredSentences = [];

