enum TtsState { playing }

class EnglishEntryaperitif extends StatefulWidget {
  const EnglishEntryaperitif({super.key});

  @override
  State<EnglishEntryaperitif> createState() => _EnglishEntryaperitifState();
}

class _EnglishEntryaperitifState extends State<EnglishEntryaperitif> {
  @override
  void initState() {
    super.initState();
    flutterTts = FlutterTts();
    flutterTts.setLanguage("en-GB");
    flutterTts.setLanguage("en-US");
    fetchSentences();
  }

  FlutterTts flutterTts = FlutterTts();

  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.setPitch(ttsPitch);
    await flutterTts.setSpeechRate(ttsSpeechRate);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Noun: aperitif (derived forms: aperitifs)
1. Alcoholic beverage taken before a meal as an appetizer
""",
  );
// 188888880002200

  final String keyword = "aperitif";
  List<Map<String, dynamic>> filteredSentences = [];

  Future<void> fetchSentences() async {
    final sentences =
        await DatabaseUtils.instance.fetchFilteredSentences(keyword: keyword);
    setState(() {
      filteredSentences = sentences;
    });
  }

  void speakEnglish(String text, {String? languageCode}) async {
    await flutterTts.setLanguage(languageCode ?? "en-GB");
    await flutterTts.speak(text);
  }

  Future<void> speakheadword(String languageCode) async {
    await flutterTts.setLanguage(languageCode);
    await flutterTts.setPitch(ttsPitch);
    await flutterTts.setSpeechRate(ttsSpeechRate);
    await flutterTts.speak("""aperitif""");
  }

  Future<void> speakaperitif144(String languageCode) async {
    await flutterTts.setLanguage(languageCode);
    await flutterTts.setPitch(ttsPitch);
    await flutterTts.setSpeechRate(ttsSpeechRate);
    await flutterTts.speak("This vermouth is a delicious aperitif.");
  }

  @override
  Widget build(BuildContext context) {
    const String videoIdend = 'vNEd9FyG5P4';
    const double startSecondsend = 83;
    const String videoIdone = 'PbQyevYlLAY';
    const double startSecondsone = 627;
    const String videoIdtwo = 'kfe9hmwjOVQ';
    const double startSecondstwo = 161;
    const String videoIdthree = 'eWg3D1SW3SI';
    const double startSecondsthree = 95;
    const String videoIdfour = 'evGHr3mMlmo';
    const double startSecondsfour = 113;
    const String videoIdfive = 'YRA_sJru4p0';
    const double startSecondsfive = 41;
    // final String _videoId = '7Z_mZ6wq1Rw';
