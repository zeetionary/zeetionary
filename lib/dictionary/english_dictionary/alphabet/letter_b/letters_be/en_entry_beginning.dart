class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  // Function to stop TTS
  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    // Update the state to reflect that TTS is stopped
    setState(() {
      isSpeaking = false;
    });
  }

  // Create an instance of EnglishMeaningConst with the desired text
  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Noun: beginning (derived forms: beginnings)
1. The event consisting of the start of something
"the beginning of the war"
 
2. The time when something begins (= commencement, first, outset, get-go [N. Amer], start, kickoff, starting time, showtime, offset)
"she knew from the beginning that he was the man for her";
 
3. The first part or section of something
"'It was a dark and stormy night' is a hackneyed beginning for a story"
 
4. The place where something begins, where it springs into being (= origin, root, rootage, source)
"the Italian beginning of the Renaissance";
 
5. The act of starting something (= start, commencement)
"he was responsible for the beginning of negotiations";

- Adjective: beginning
1. Of or relating to the initial or early part of something (= first)
"the beginning canto of the poem";
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

