                            """١. (ناو) لێکۆڵینەوە کاری چاندنی گوڵ، سەوزە، و میوە"""),
                    SentencesRow(
                      englishText:
                          """He is passionate about horticulture and grows his own garden.""",
                      kurdishText:
                          """حەزی بە باخەوانی و باخچەی خۆی بەڕێوە دەبات.""",
                      onPressedBritish: () => speaksentence1("en-GB"),
                      onPressedAmerican: () => speaksentence1("en-US"),
                    ),
                  ],
