    const double startSecondstwo = 697;
    const String videoIdthree = 'BpnF1HCQ0Zo';
    const double startSecondsthree = 437;
    const String videoIdfour = 'LXlgSvch6eg';
    const double startSecondsfour = 137;
    const String videoIdfive = 'aSe8Mx6X6Fg';
    const double startSecondsfive = 49;

    return DefaultTabController(
      length: 4,
      child: Scaffold(
        body: NestedScrollView(
          headerSliverBuilder: (context, innerBoxIsScrolled) {
            return [
              CustomSliverAppBar(
                flexibleSpace: FlexibleSpaceBar(
                  background: SingleChildScrollView(
                    child: EntryPageColumn(
                      word: """inferiority complex""",
                      // alsoEnglishWord: "also: inferiority complex",
