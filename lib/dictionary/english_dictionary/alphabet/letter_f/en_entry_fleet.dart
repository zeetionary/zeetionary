class _EnglishEntryfleetState extends State<EnglishEntryfleet> {
  @override
  void initState() {
    super.initState();
    flutterTts = FlutterTts();
    flutterTts.setLanguage("en-GB");
    flutterTts.setLanguage("en-US");
    fetchSentences();
  }

  FlutterTts flutterTts = FlutterTts();

  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Noun: fleet (derived forms: fleets)
1. Group of aircraft operating together under the same ownership
 
2. Group of motor vehicles operating together under the same ownership
 
3. A group of ships travelling together or operating under the same ownership
 
4. A group of warships organized as a tactical unit

- Adjective: fleet (derived forms: fleetest, fleeter)
1. Moving very fast (= swift)
"fleet of foot"; "the fleet scurrying of squirrels";

- Verb: fleet (derived forms: fleeted, fleeting, fleets)
1. Move along rapidly and lightly; skim or dart (= flit, flutter, dart)
"The hummingbird fleeted among the branches";
 
2. Disappear gradually (= evanesce, fade, blow over, pass off, pass)
""",
  );

  final String keyword = "fleet";
  List<Map<String, dynamic>> filteredSentences = [];

  Future<void> fetchSentences() async {
    final sentences =
        await DatabaseUtils.instance.fetchFilteredSentences(keyword: keyword);
    setState(() {
      filteredSentences = sentences;
    });
  }

  void speakEnglish(String text, {String? languageCode}) async {
    await flutterTts.setLanguage(languageCode ?? "en-GB");
    await flutterTts.speak(text);
  }

  Future<void> speakheadword(String languageCode) async {
    await flutterTts.setLanguage(languageCode);

    await flutterTts.speak("""fleet""");
  }

  Future<void> speaksentence1(String languageCode) async {
    await flutterTts.setLanguage(languageCode);

    await flutterTts.speak(
        """Nelson destroyed the Franco-Spanish fleet at the Battle of Trafalgar.""");
  }

  Future<void> speaksentence2(String languageCode) async {
    await flutterTts.setLanguage(languageCode);

    await flutterTts.speak("""The entire fleet was sunk.""");
  }

  Future<void> speaksentence3(String languageCode) async {
    await flutterTts.setLanguage(languageCode);

    await flutterTts.speak("""The whaling fleet set sail at dawn.""");
  }

  Future<void> speaksentence4(String languageCode) async {
    await flutterTts.setLanguage(languageCode);

    await flutterTts
        .speak("""The fleet was mobilized and the country prepared for war.""");
  }

  Future<void> speaksentence5(String languageCode) async {
    await flutterTts.setLanguage(languageCode);

    await flutterTts
        .speak("""The airline is doubling the size of its fleet.""");
  }

  @override
  Widget build(BuildContext context) {
    return DefaultTabController(
      length: 4,
