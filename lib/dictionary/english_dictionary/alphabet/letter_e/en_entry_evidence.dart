class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Noun: evidence (derived forms: evidenced, evidences, evidencing)
1. Your basis for belief or disbelief; knowledge on which to base belief (= grounds)
"the evidence that smoking causes lung cancer is very compelling";
 
2. An indication that makes something evident
"his trembling was evidence of his fear"
 
3. (law) all the means by which any alleged matter of fact whose truth is investigated at judicial trial is established or disproved

- Verb: evidence
1. Provide evidence for; stand as proof of; show by one's behaviour, attitude, or external attributes (= attest, certify, manifest, demonstrate)
 
2. (law) provide evidence for (= testify, bear witness, prove, show, witness)
 
3. Give evidence (= tell)
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

