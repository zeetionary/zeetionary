                    ),
                    const DividerSentences(),
                    SentencesRow(
                      englishText:
                          """He made an obscene gesture with his hand.""",
                      kurdishText: """ئاماژەیەکی بێ‌ڕێزانەی بە دەستی کرد.""",
                      onPressedBritish: () => speaksentence3("en-GB"),
                      onPressedAmerican: () => speaksentence3("en-US"),
                    ),
                    const DividerSentences(),
