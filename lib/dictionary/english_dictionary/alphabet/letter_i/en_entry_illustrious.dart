    const double startSecondsend = 621;
    const String videoIdone = '6781XugwQrA';
    const double startSecondsone = 648;
    const String videoIdtwo = 'Y80ZTbjtz5c';
    const double startSecondstwo = 213;
    const String videoIdthree = 'WPqT6ZoyOuw';
    const double startSecondsthree = 70;
    const String videoIdfour = 'fIgi83Eyn-Y';
    const double startSecondsfour = 224;
    const String videoIdfive = 'dOkFXPblLpU';
    const double startSecondsfive = 216;

    return DefaultTabController(
      length: 4,
      child: Scaffold(
        body: NestedScrollView(
          headerSliverBuilder: (context, innerBoxIsScrolled) {
            return [
              CustomSliverAppBar(
                flexibleSpace: FlexibleSpaceBar(
                  background: SingleChildScrollView(
                    child: EntryPageColumn(
                      word: """illustrious""",
                      // alsoEnglishWord: "also: illustrious",
                      britshText: """IpaUK: /ɪˈlʌstriəs/""",
                      americanText: """IpaUS: /ɪˈlʌstriəs/""",
                      onPressedBritish: () => speakheadword("en-GB"),
                      onPressedAmerican: () => speakheadword("en-US"),
                    ),
                  ),
                ),
                bottom: const CustomTabBarNew(
                  tabs: [
                    UkIconForTab(),
                    KurdIconForTab(),
                    SentencesIconForTab(),
                    VideoIconForTab(),
                  ],
                ),
              ),
            ];
          },
          body: TabBarView(
            children: [
              SingleChildScrollView(
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    EnglishButtonTTS(
                      onBritishPressed: (languageCode) =>
                          startSpeaking(languageCode, englishMeaningConst),
                      onAmericanPressed: (languageCode) =>
                          startSpeaking(languageCode, englishMeaningConst),
                      onStopPressed: stopSpeaking,
                    ),
                    englishMeaningConst,
                  ],
                ),
              ),
              SingleChildScrollView(
                child: CustomColumnWidget(
                  children: [
                    const KurdishVocabulary(text: """
کوردی: بەنێوبانگ، نێوبەدەرەوە، ناسراو
"""),
// With short examples define "illustrious", please follow LX instructions
