٢. (ھاوەڵکار) بەشێوەیەک کە ھەوڵی بۆ دەدەیت و چاوەڕێی شانس ناکەیت"""),
                          SentencesRow(
                            englishText: "She was actively looking for a job.",
                            kurdishText: "بە جدییانە بۆ کارێک دەگەڕا.",
