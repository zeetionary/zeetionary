class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Adjective: exhausted
1. Extremely exhausted (= knackered [Brit, informal], drained, zonked [informal], zonked out [informal], stonkered [Brit, Austral, NZ, informal], deadbeat [Brit, informal], shagged out [Brit, informal], shagged [Brit, informal], shattered [Brit, informal], dog-tired [informal], fagged [Brit, informal], fatigued, played out, spent, washed-out, worn-out, worn out, gone, shot [informal], fried [N. Amer, informal], pooped [N. Amer, informal])
"the day's shopping left her exhausted";
 
2. Depleted of content, energy, force, strength; have nothing left (= spent, done)
"impossible to grow tobacco on the exhausted soil"; "the exhausted food sources"; "exhausted oil wells";
 
3. Drained physically
"the day's events left her completely exhausted--her strength drained"
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

