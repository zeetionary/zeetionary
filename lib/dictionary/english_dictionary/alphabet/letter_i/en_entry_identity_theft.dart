                          """ژمارەی تاوانی دزینی ناسنامە لە بەریتانیا ساڵی ڕابردوو بۆ ٤٢٠٢٩ بەرزبوویەوە.""",
                      onPressedBritish: () => speaksentence1("en-GB"),
