                          """He spoke with the housemaster about his behavior.""",
                      kurdishText:
                          """لەگەڵ مامۆستاکە قسەی کرد سەبارەت بە هەڵسووکەوتی.""",
                      onPressedBritish: () => speaksentence1("en-GB"),
                      onPressedAmerican: () => speaksentence1("en-US"),
