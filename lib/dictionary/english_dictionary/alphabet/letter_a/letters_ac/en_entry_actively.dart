                          SentencesRow(
                            englishText:
                                "Your proposal is being actively considered.",
                            kurdishText:
                                "پێشنیارەکەت بەڕشتی کاری لەسەر دەکرێت.",
                            onPressedBritish: () => speakactiv2537("en-GB"),
