class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

// Function to stop TTS
  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    // Update the state to reflect that TTS is stopped
    setState(() {
      isSpeaking = false;
    });
  }

// Create an instance of EnglishMeaningConst with the desired text
  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Adjective: crimson 
1. Of a colour at the end of the colour spectrum (next to orange); resembling the colour of blood, cherries, tomatoes or rubies (= red, reddish, ruddy, blood-red, carmine, cerise, cherry, cherry-red, ruby, ruby-red, scarlet, rufescent, reddy)
"The picture would come through dark with a crimson tinge";
 
2. Characterized by violence or bloodshed (= red, violent)
"writes of crimson deeds and barbaric days"; "fann'd by Conquest's crimson wing";
 
3. (especially of the face) reddened or suffused with or as if with blood from emotion or exertion (= red, reddened, red-faced, flushed)
"crimson with fury"; "flushed (or crimson) with embarrassment";

- Noun: crimson (derived forms: crimsons)
1. A deep and vivid red colour (= ruby, deep red)

- Verb: crimson (derived forms: crimsoned, crimsons, crimsoning)
1. Turn red, as if in embarrassment or shame (= blush, flush, redden)
"The girl crimsoned when a young man whistled as she walked by";
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const DividerDefinition(),

          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          // Speaker icon for American English
          englishMeaningConst,
        ],
      ),
    );
  }
}

