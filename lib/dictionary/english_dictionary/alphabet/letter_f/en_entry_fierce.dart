2. Marked by extreme intensity of emotions or convictions; inclined to react violently; fervid (= tearing, vehement, violent, trigger-happy)
"fierce loyalty";
 
3. Ruthless in competition (= cutthroat, bowelless)
"fierce competition";
 
4. Violently agitated and turbulent (= boisterous, rough)
"the fierce thunders roar me their music";
""",
