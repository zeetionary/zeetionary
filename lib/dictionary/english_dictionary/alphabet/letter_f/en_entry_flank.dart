                      britshText: """IpaUK: /flæŋk/""",
                      americanText: """IpaUS: /flæŋk/""",
                      onPressedBritish: () => speakheadword("en-GB"),
                      onPressedAmerican: () => speakheadword("en-US"),
                    ),
                  ),
                ),
                automaticallyImplyLeading: false,
                bottom: const TabBar(
                  tabs: [
                    UkIconForTab(),
                    KurdIconForTab(),
                    SentencesIconForTab(),
                    VideoIconForTab(),
                  ],
                ),
              ),
            ];
          },
          body: TabBarView(
            children: [
              SingleChildScrollView(
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    const DividerDefinition(),
                    EnglishButtonTTS(
                      onBritishPressed: (languageCode) =>
                          startSpeaking(languageCode, englishMeaningConst),
                      onAmericanPressed: (languageCode) =>
                          startSpeaking(languageCode, englishMeaningConst),
                      onStopPressed: stopSpeaking,
                    ),
                    englishMeaningConst,
                  ],
                ),
              ),
              SingleChildScrollView(
                child: CustomColumnWidget(
                  children: [
                    const DividerDefinition(),
                    const KurdishVocabulary(text: """
کوردی: کەلەکە، خاڵێگە، پووتەگا، تەنیشت، برک، لاقەد، لاپاڵ، قەبرغە، نیڤ‌تەنگ (لەش)،	گۆشتی کەلەکە، تەڕاف،	لا، تەنیشت (خانوو)،	(سەربازی) باڵ، قۆڵ، باسک
"""),
// With short examples define "flank", please follow LX instructions
                    const DefinitionKurdish(
                        text:
                            """١. (ناو) لای شتێک، بۆ نموونە بینا یان شاخێک"""),
                    SentencesRow(
                      englishText:
                          """The sun lit up the western flank of the hill.""",
                      kurdishText: """خۆرەکە لای ڕاستی گردەکەی ڕووناککردەوە.""",
                      onPressedBritish: () => speaksentence1("en-GB"),
                      onPressedAmerican: () => speaksentence1("en-US"),
                    ),
                    const DividerDefinition(),
                    const DefinitionKurdish(
                        text:
                            """٢. (ناو) ڕاست و چەپی سوپایەک یان تیمێکی وەرزشی لە کاتی یاریدا"""),
                    SentencesRow(
                      englishText:
                          """Our orders were to attack their left flank.""",
                      kurdishText:
                          """فەرمانەکانمان ئەوە بوو ھێرش بکەینە سەر باڵی چەپ.""",
                      onPressedBritish: () => speaksentence2("en-GB"),
                      onPressedAmerican: () => speaksentence2("en-US"),
                    ),
                    const DividerSentences(),
                    SentencesRow(
                      englishText:
                          """Their centre made a dash down the right flank.""",
                      kurdishText: """ناوەڕاستیان ھێرشێکی بۆ باڵی ڕاست کرد.""",
                      onPressedBritish: () => speaksentence3("en-GB"),
                      onPressedAmerican: () => speaksentence3("en-US"),
                    ),
                    const DividerDefinition(),
                    const DefinitionKurdish(
                        text:
                            """٣. (کردار) ھەبوونی کەسێک/شتێک لە لایەک یان ھەر دوو لای"""),
                    SentencesRow(
                      englishText:
                          """She left the courtroom flanked by armed guards.""",
                      kurdishText:
                          """ژووری دادگای جێھێشت دەوردراو بە پاسەوانی چەکدار.""",
                      onPressedBritish: () => speaksentence4("en-GB"),
                      onPressedAmerican: () => speaksentence4("en-US"),
                    ),
                    const DividerSentences(),
                    SentencesRow(
                      englishText:
                          """The president was flanked by senior advisors.""",
                      kurdishText:
                          """سەرۆک ڕاوێژکارانی باڵای وەک یاوەری ھەبوو.""",
                      onPressedBritish: () => speaksentence5("en-GB"),
                      onPressedAmerican: () => speaksentence5("en-US"),
                    ),
                  ],
                ),
              ),
              Consumer(
                builder: (context, ref, child) {
                  if (filteredSentences.isEmpty) {
                    return const NoSentencesFromDatabase();
                  } else {
                    return ListView.builder(
                      itemCount: filteredSentences.length,
                      itemBuilder: (context, index) {
                        final sentence = filteredSentences[index];
                        final showDivider = filteredSentences.length > 1 &&
                            index != filteredSentences.length - 1;
                        return CustomSentenceWidget(
                          englishText: sentence['english'].toString(),
                          frenchText: sentence['french'].toString(),
                          keyword: keyword,
                          onPressedBritish: () => speakEnglish(
                            sentence['english'].toString(),
                            languageCode: "en-GB",
                          ),
                          onPressedAmerican: () => speakEnglish(
                            sentence['english'].toString(),
                            languageCode: "en-US",
                          ),
                          showDivider: showDivider,
                        );
                      },
                    );
                  }
                },
              ),
              const YouTubeScroller(
                children: [
                  YoutubeEmbeddedone(),
                  YoutubeEmbeddedtwo(),
                  YoutubeEmbeddedthree(),
                  YoutubeEmbeddedfour(),
                  YoutubeEmbeddedfive(),
                  // YoutubeEmbeddedsix(),
                  // YoutubeEmbeddedseven(),
                  // YoutubeEmbeddedeight(),
