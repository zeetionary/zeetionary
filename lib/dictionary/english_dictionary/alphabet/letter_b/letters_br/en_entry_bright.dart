class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    // Update the state to reflect that TTS is in progress
    setState(() {
      isSpeaking = true;
    });
  }

  // Function to stop TTS
  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    // Update the state to reflect that TTS is stopped
    setState(() {
      isSpeaking = false;
    });
  }

  // Create an instance of EnglishMeaningConst with the desired text
  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Adjective: bright (= derived forms: brighter, brightest)
1. Having strong or striking colour (= brilliant, vivid)
"bright dress";

2. Emitting or reflecting light readily or in large amounts
"the sun was bright and hot"; "a bright sunlit room"
 
3. Characterized by quickness and ease in learning (= smart)
"some children are brighter in one subject than another";

4. Having lots of light either natural or artificial
"the room was bright and airy"; "a stage bright with spotlights"
 
5. Made smooth and bright by or as if by rubbing; reflecting a sheen or glow (= burnished, lustrous, shining, shiny)
"bright silver candlesticks";
 
6. Splendid
"the bright stars of stage and screen"; "a bright moment in history"; "the bright pageantry of court"
 
7. Clear and sharp and ringing (= brilliant)
"the bright sound of the trumpet section";
 
8. Characterized by happiness or gladness
"bright faces"; "all the world seems bright and gay"
 
9. Likely to turn out well in the future (= hopeful, promising)
"had a bright future in publishing";
 
10. Not made dim or less bright (= undimmed)
"bright headlights";

- Adverb: bright
1. With brightness (= brilliantly, brightly)
"the windows glowed jewel bright";
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const DividerDefinition(),

          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          // Speaker icon for American English
          englishMeaningConst,
        ],
      ),
    );
  }
}

