    text: """
- Noun: hassle (Derived forms: hassles)
Usage: informal
1. Something annoying or fiddly that takes some time to do (- faff [Brit, informal], bother)
"it was a bit of a hassle";
 
2. An angry disturbance (- fuss, trouble, bother, stink [informal], shindy [informal])
 
3. Disorderly fighting (- scuffle, tussle, dogfight, rough-and-tumble)

- Verb: hassle (Derived forms: hassled, hassling, hassles)
Usage: informal
1. Annoy or hinder continually or in many ways (- harass, harry, chivy, chivvy, chevy, chevvy [non-standard], beset, plague, molest, provoke)
"This man hassles his female co-workers";
""",
  );

