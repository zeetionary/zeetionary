    const double startSecondstwo = 1725;
    const String videoIdthree = 'XPzrySKqRP0';
    const double startSecondsthree = 7;
    const String videoIdfour = '9Hjj_L8cq00';
    const double startSecondsfour = 702;
    const String videoIdfive = '_XHXKf2ThOQ';
    const double startSecondsfive = 576;

    return DefaultTabController(
      length: 4,
      child: Scaffold(
        body: NestedScrollView(
