import 'package:flutter/material.dart';
import 'package:flutter_tts/flutter_tts.dart';
import 'package:flutter_riverpod/flutter_riverpod.dart';
import 'package:zeetionary/constants.dart';

enum TtsState { playing }

class EnglishEntryanxiously extends StatefulWidget {
  const EnglishEntryanxiously({super.key});

  @override
  State<EnglishEntryanxiously> createState() => _EnglishEntryanxiouslyState();
}

class _EnglishEntryanxiouslyState extends State<EnglishEntryanxiously> {
  @override
  void initState() {
    super.initState();
    flutterTts = FlutterTts();
    flutterTts.setLanguage("en-GB");
    flutterTts.setLanguage("en-US");
    fetchSentences();
  }

  FlutterTts flutterTts = FlutterTts();

  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.setPitch(ttsPitch);
    await flutterTts.setSpeechRate(ttsSpeechRate);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Adverb: anxiously 
1. With anxiety or apprehension (= uneasily, apprehensively)
"we watched anxiously";
""",
  );
// 188888880002200

  final String keyword = "anxiously";
  List<Map<String, dynamic>> filteredSentences = [];

  Future<void> fetchSentences() async {
    final sentences =
        await DatabaseUtils.instance.fetchFilteredSentences(keyword: keyword);
    setState(() {
      filteredSentences = sentences;
    });
  }

  void speakEnglish(String text, {String? languageCode}) async {
    await flutterTts.setLanguage(languageCode ?? "en-GB");
    await flutterTts.speak(text);
  }

  Future<void> speakheadword(String languageCode) async {
    await flutterTts.setLanguage(languageCode);
    await flutterTts.setPitch(ttsPitch);
    await flutterTts.setSpeechRate(ttsSpeechRate);
    await flutterTts.speak("""anxiously""");
  }

  Future<void> speakanxiously751(String languageCode) async {
    await flutterTts.setLanguage(languageCode);
    await flutterTts.setPitch(ttsPitch);
    await flutterTts.setSpeechRate(ttsSpeechRate);
    await flutterTts.speak("We waited anxiously by the phone.");
  }

  @override
  Widget build(BuildContext context) {
    const String videoIdend = 'wLU6CsY9FNw';
    const double startSecondsend = 1;
    const String videoIdone = 'YeDEma9Iiac';
    const double startSecondsone = 109;
    const String videoIdtwo = 'knjliFs3gR8';
    const double startSecondstwo = 791;
    const String videoIdthree = '25wC533f7dQ';
    const double startSecondsthree = 61;
    const String videoIdfour = 'UOIFBKB4mIE';
    const double startSecondsfour = 120;
    const String videoIdfive = 'tfn-LC254Hk';
    const double startSecondsfive = 205;
    // final String _videoId = 'T8uc3NzU7J4';
    // final double _startSeconds = 1477;
