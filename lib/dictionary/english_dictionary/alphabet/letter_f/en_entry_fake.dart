class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Adjective: fake
1. Fraudulent; having a misleading appearance (= bogus, phony [N. Amer], phoney, cod [Brit, informal])
"A claim can be considered fake, or false, when the speaker promises too much";
 
2. Not genuine or real; being an imitation of the genuine article (= false, faux, imitation, simulated)
"it isn't fake anything; it's real synthetic fur";

- Noun: fake (derived forms: fakes)
1. Something that is a counterfeit; not what it seems to be (= sham, postiche)
 
2. A person who makes deceitful pretences (= imposter, impostor, pretender, faker, fraud, sham, shammer, pseudo, pseud, role player)
 
3. (football) a deceptive move made by a football player (= juke [N. Amer, informal])

- Verb: fake (derived forms: faked, fakes, faking)
1. Make a copy of with the intent to deceive (= forge, counterfeit)
"he faked the signature";
 
2. Tamper, with the purpose of deception (= fudge, manipulate, falsify, cook [informal], wangle [informal], misrepresent, spoof [informal])
"fake the figures";
 
3. [US, informal] Speak insincerely or without regard for facts or truths (= talk through one's hat [informal], bull [informal])
"The politician was not well prepared for the debate and faked it";
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const DividerDefinition(),
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

