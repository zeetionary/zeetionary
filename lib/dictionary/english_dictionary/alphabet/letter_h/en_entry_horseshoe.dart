    const double startSecondstwo = 769;
    const String videoIdthree = 'qr4MSVxfYBE';
    const double startSecondsthree = 1050;
    const String videoIdfour = 'wJa5Ch0O4BI';
    const double startSecondsfour = 1655;
    const String videoIdfive = '__qtH1ly2Sg';
    const double startSecondsfive = 544;

    return DefaultTabController(
      length: 4,
      child: Scaffold(
        body: NestedScrollView(
          headerSliverBuilder: (context, innerBoxIsScrolled) {
            return [
              CustomSliverAppBar(
                flexibleSpace: FlexibleSpaceBar(
                  background: SingleChildScrollView(
                    child: EntryPageColumn(
                      word: """horseshoe""",
