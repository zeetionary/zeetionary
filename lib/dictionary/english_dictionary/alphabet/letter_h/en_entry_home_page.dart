                        text:
                            """٢. (ناو) پەڕەی سەرەکی کە لەسەر وێبگەڕەکەت دەردەکەوێت کاتێک دەیکەیتەوە"""),
                    const DividerSentences(),
                    SentencesRow(
                      englishText:
                          """To make this your home page, click here.""",
