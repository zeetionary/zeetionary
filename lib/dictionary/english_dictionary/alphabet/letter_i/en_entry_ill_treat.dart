    const String videoIdtwo = 'CDxFPGHk9Vw';
    const double startSecondstwo = 1578;
    const String videoIdthree = '1m5_rxrpsL4';
    const double startSecondsthree = 160;
    const String videoIdfour = 'CKYw_RG7i7w';
    const double startSecondsfour = 603;
    const String videoIdfive = 'WUVdAyZTsGI';
    const double startSecondsfive = 640;

    return DefaultTabController(
      length: 4,
      child: Scaffold(
        body: NestedScrollView(
          headerSliverBuilder: (context, innerBoxIsScrolled) {
            return [
              CustomSliverAppBar(
                flexibleSpace: FlexibleSpaceBar(
                  background: SingleChildScrollView(
                    child: EntryPageColumn(
                      word: """ill-treat""",
                      // alsoEnglishWord: "also: ill-treat",
                      britshText: """IpaUK: /ˌɪl ˈtriːt/""",
                      americanText: """IpaUS: /ˌɪl ˈtriːt/""",
                      onPressedBritish: () => speakheadword("en-GB"),
                      onPressedAmerican: () => speakheadword("en-US"),
                    ),
                  ),
                ),
                bottom: const CustomTabBarNew(
                  tabs: [
                    UkIconForTab(),
                    KurdIconForTab(),
                    SentencesIconForTab(),
                    VideoIconForTab(),
                  ],
                ),
              ),
            ];
          },
          body: TabBarView(
            children: [
              SingleChildScrollView(
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    EnglishButtonTTS(
                      onBritishPressed: (languageCode) =>
                          startSpeaking(languageCode, englishMeaningConst),
                      onAmericanPressed: (languageCode) =>
                          startSpeaking(languageCode, englishMeaningConst),
                      onStopPressed: stopSpeaking,
                    ),
                    englishMeaningConst,
                  ],
                ),
              ),
              SingleChildScrollView(
                child: CustomColumnWidget(
                  children: [
//                     const KurdishVocabulary(text: """
