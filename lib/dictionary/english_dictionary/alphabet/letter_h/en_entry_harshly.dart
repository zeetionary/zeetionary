                            """١. (ھاوەڵکار) بەشێوەیەک کە توند و نامیھرەبانانەیە"""),
                    SentencesRow(
                      englishText: """She was treated very harshly.""",
                      kurdishText: """زۆر بە توندی مامەڵەی لەگەڵ کرا.""",
                      onPressedBritish: () => speaksentence1("en-GB"),
                      onPressedAmerican: () => speaksentence1("en-US"),
                    ),
                    const DividerSentences(),
                    SentencesRow(
                      englishText:
                          """The decision was harshly criticized by environmental groups.""",
                      kurdishText:
                          "بڕیارەکە بە توندی لەلایەن گرووپە ژینگەییەکانەوە ڕەخنەی لێگیردرا.",
                      onPressedBritish: () => speaksentence2("en-GB"),
                      onPressedAmerican: () => speaksentence2("en-US"),
                    ),
                    const DividerSentences(),
                    SentencesRow(
                      englishText:
                          """His findings have been harshly criticized by fellow scientists.""",
                      kurdishText:
                          """دۆزینەوەکانی بە توندی ڕەخنەیان لێگیراوە لەلایەن زانا ھاوکارەکانی.""",
                      onPressedBritish: () => speaksentence3("en-GB"),
                      onPressedAmerican: () => speaksentence3("en-US"),
                    ),
                    const DividerDefinition(),
                    const DefinitionKurdish(
                        text:
                            """٢. (ھاوەڵکار) بەشێوەیەک کە زۆر بەھێز و ڕۆشنە"""),
                    SentencesRow(
                      englishText: """The room was harshly lit.""",
                      kurdishText: """ژوورەکە بە توندی ڕۆشن کرابوویەوە.""",
                      onPressedBritish: () => speaksentence4("en-GB"),
                      onPressedAmerican: () => speaksentence4("en-US"),
                    ),
                    const DividerDefinition(),
                    const DefinitionKurdish(
                        text:
                            """٣. (ھاوەڵکار) بە شێوەیەک کە ناخۆشە بۆ گوێگرتن لێی"""),
                    SentencesRow(
                      englishText:
                          """The bell sounded, ringing harshly in the quiet classroom.""",
                      kurdishText:
                          """زەنگەکە لێی دا، بە توندی دەنگی دەھات لە پۆلە بێ‌دەنگەکەدا.""",
                      onPressedBritish: () => speaksentence5("en-GB"),
                      onPressedAmerican: () => speaksentence5("en-US"),
                    ),
                    const DividerDefinition(),
                    const DefinitionKurdish(
                        text:
                            """٤. (ھاوەڵکار) بە شێوەیەک کە زۆر بەھێزە کە دەکرێت زیانبەخش بێت"""),
                    SentencesRow(
                      englishText:
                          """The shovel scraped harshly over the floor.""",
                      kurdishText: """بێڵەکە بە توندی بە زەوییەکەدا دەکیڕا.""",
                      onPressedBritish: () => speaksentence6("en-GB"),
                      onPressedAmerican: () => speaksentence6("en-US"),
                    ),
                  ],
                ),
              ),
              Consumer(
                builder: (context, ref, child) {
                  if (filteredSentences.isEmpty) {
                    return const NoSentencesFromDatabase();
                  } else {
                    return ListView.builder(
                      itemCount: filteredSentences.length,
                      itemBuilder: (context, index) {
                        final sentence = filteredSentences[index];
                        final showDivider = filteredSentences.length > 1 &&
                            index != filteredSentences.length - 1;
                        return CustomSentenceWidget(
                          englishText: sentence['english'].toString(),
                          frenchText: sentence['french'].toString(),
                          keyword: keyword,
                          onPressedBritish: () => speakEnglish(
                            sentence['english'].toString(),
                            languageCode: "en-GB",
                          ),
                          onPressedAmerican: () => speakEnglish(
                            sentence['english'].toString(),
                            languageCode: "en-US",
                          ),
                          showDivider: showDivider,
                        );
                      },
                    );
                  }
                },
              ),
              const YouTubeScroller(
                children: [
                  YoutubeEmbeddedone(),
                  YoutubeEmbeddedtwo(),
                  YoutubeEmbeddedthree(),
                  YoutubeEmbeddedfour(),
                  YoutubeEmbeddedfive(),
                  // YoutubeEmbeddedsix(),
                  // YoutubeEmbeddedseven(),
