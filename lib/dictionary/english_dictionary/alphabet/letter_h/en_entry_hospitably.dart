                            """١. (ھاوەڵکار) میواندۆستانە؛ کە دڵخۆشیت بە هەبوونی میوان"""),
                    SentencesRow(
                      englishText: """He welcomed me most hospitably.""",
                      kurdishText: """زۆر میواندۆستانە پێشوازی لێم کرد.""",
