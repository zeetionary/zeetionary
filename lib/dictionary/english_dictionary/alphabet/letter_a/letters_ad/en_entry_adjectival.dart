    const double startSecondstwo = 2437;
    const String videoIdthree = 'TbpPw4x5Z1A';
    const double startSecondsthree = 181;
    const String videoIdfour = 'PMBSiWEk6tA';
    const double startSecondsfour = 6924;
    const String videoIdfive = 'WLEHULGODIc';
    const double startSecondsfive = 2232;
    // const String videoId = '3sKwBpEeH90';
    // const double startSeconds = 1878;

    return DefaultTabController(
      length: 4,
      child: Scaffold(
        body: NestedScrollView(
          headerSliverBuilder: (context, innerBoxIsScrolled) {
            return [
              CustomSliverAppBar(
                flexibleSpace: FlexibleSpaceBar(
                  background: SingleChildScrollView(
                    child: EntryPageColumn(
                      word: """adjectival""",
                      // alsoEnglishWord: "also: adjectival",
                      britshText: """IpaUK: /ˌædʒekˈtaɪvl/""",
                      americanText: """IpaUS: /ˌædʒekˈtaɪvl/""",
                      onPressedBritish: () => speakheadword("en-GB"),
                      onPressedAmerican: () => speakheadword("en-US"),
                    ),
                  ),
                ),
                bottom: const CustomTabBarNew(
                  tabs: [
                    UkIconForTab(),
