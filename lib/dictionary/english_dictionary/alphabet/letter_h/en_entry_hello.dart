2. A call for response if it is not clear whether anyone is present or listening (- hullo, hallo)
 
3. Used sarcastically to imply that something is foolish or unrealistic (- hullo, hallo)

- Noun: hello (Derived forms: hellos)
1. An expression of greeting (- hullo, hi, howdy, how-do-you-do, hallo)
"every morning they exchanged polite hellos";

- Verb: hello (Derived forms: helloed, helloes, helloing)
1. Shout or greet with hello
""",
