    const double startSecondstwo = 1103;
    const String videoIdthree = 'XVuIMocTciY';
    const double startSecondsthree = 585;
    const String videoIdfour = 'i44KnocYsno';
    const double startSecondsfour = 632;
    const String videoIdfive = 'o0A9M5wHBA4';
    const double startSecondsfive = 633;

    return DefaultTabController(
      length: 4,
      child: Scaffold(
        body: NestedScrollView(
          headerSliverBuilder: (context, innerBoxIsScrolled) {
            return [
              CustomSliverAppBar(
                flexibleSpace: FlexibleSpaceBar(
                  background: SingleChildScrollView(
                    child: EntryPageColumn(
                      word: """indescribably""",
                      // alsoEnglishWord: "also: indescribably",
