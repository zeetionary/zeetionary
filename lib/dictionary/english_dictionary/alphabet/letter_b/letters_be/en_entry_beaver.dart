class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  // Function to stop TTS
  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    // Update the state to reflect that TTS is stopped
    setState(() {
      isSpeaking = false;
    });
  }

  // Create an instance of EnglishMeaningConst with the desired text
  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Noun: beaver (derived forms: beavers, beavering, beavered)
1. Large semiaquatic rodent with webbed hind feet and a broad flat tail; construct complex dams and underwater lodges
 
2. The soft brown fur of the beaver (= beaver fur)
 
3. A full beard
 
4. A man's hat with a tall crown; usually covered with silk or with beaver fur (= dress hat, high hat, opera hat, silk hat, stovepipe, top hat, topper, stovepipe hat)
 
5. A movable piece of armour on a medieval helmet used to protect the lower face
 
6. A hat made with the fur of a beaver (or similar material) (= castor)
 
7. [vulgar] Vulgar term for a woman's pubic hair or genitals

- Verb: beaver (derived forms: beavers, beavering, beavered)
Usage: informal
1. Work hard on something (= beaver away [informal], slave away, grind away)

- Noun: Beaver (derived forms: Beavers)
1. A native or resident of Oregon (= Oregonian)
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const DividerDefinition(),

          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          // Speaker icon for American English
          englishMeaningConst,
        ],
      ),
    );
  }
}

