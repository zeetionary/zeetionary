2. Enter unlawfully on someone's property (- trespass)
"Don't intrude on my land!";
 
3. Search or inquire in a meddlesome way (- horn in, pry, nose, poke)
"This guy is always intruding around the office";
 
4. Thrust oneself in as if by force (- obtrude)
"The colours don't intrude on the viewer";
""",
  );

