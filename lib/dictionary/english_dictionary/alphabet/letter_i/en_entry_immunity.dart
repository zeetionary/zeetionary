- Noun: immunity (Derived forms: immunities)
1. The state of not being susceptible (- unsusceptibility)
"immunity to rust";
 
2. (medicine) the condition in which an organism can resist disease (- resistance)
 
3. The quality of being unaffected by something
"immunity to criticism"
 
4. An act exempting someone (- exemption, granting immunity)
"he was granted immunity from prosecution";
""",
  );

