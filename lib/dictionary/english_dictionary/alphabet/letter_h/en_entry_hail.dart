  FlutterTts flutterTts = FlutterTts();

  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.setPitch(ttsPitch);
    await flutterTts.setSpeechRate(ttsSpeechRate);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Verb: hail (Derived forms: hailed, hailing, hails)
1. Praise vociferously (- acclaim, herald)
"The critics hailed the young pianist as a new Rubinstein";
 
2. Be a native of (- come)
"She hails from Kalamazoo";
 
3. Call for
"hail a cab"
 
4. Greet enthusiastically or joyfully (- herald)
 
5. Precipitate as small ice particles
"It hailed for an hour"

- Noun: hail (Derived forms: hails)
1. Precipitation of ice pellets when there are strong rising air currents
 
2. Many objects thrown forcefully through the air
"a hail of pebbles"; "a hail of bullets"
 
3. Enthusiastic greeting
""",
  );

