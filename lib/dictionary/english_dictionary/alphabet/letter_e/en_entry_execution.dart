class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Noun: execution (derived forms: executions)
1. The act of performing; of doing something successfully; using knowledge as distinguished from merely possessing it (= performance, carrying out, carrying into action)
 
2. The act of accomplishing some aim or executing some order (= implementation, carrying out)
"the agency was created for the execution of the policy";
 
3. Unlawful premeditated killing of a human being by a human being (= murder, slaying)
 
4. Putting a condemned person to death (= executing, capital punishment, death penalty)
 
5. (computing) the process of carrying out an instruction by a computer (= instruction execution)
 
6. (law) the completion of a legal instrument (such as a contract or deed) by signing it (and perhaps sealing and delivering it) so that it becomes legally binding and enforceable (= execution of instrument)
 
7. (law) a routine court order that attempts to enforce the judgment that has been granted to a plaintiff by authorizing a sheriff to carry it out (= writ of execution)
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const DividerDefinition(),
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

