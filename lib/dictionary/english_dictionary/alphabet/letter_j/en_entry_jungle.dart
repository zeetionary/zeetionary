                    const DefinitionKurdish(
                        text:
                            """١. (ناو) ناوچەیەک لە دارستانی خولگەیی کە تێیدا دار و ڕووەک بە چڕی گەشە دەکەن"""),
                    SentencesRow(
                      englishText: """The area was covered in dense jungle.""",
                      kurdishText: """ناوچەکە بە جەنگەڵی چڕ داپۆشرا بوو.""",
                      onPressedBritish: () => speaksentence1("en-GB"),
                      onPressedAmerican: () => speaksentence1("en-US"),
                    ),
                    const DividerDefinition(),
                    const DefinitionKurdish(text: """ژممر. (ناو) پننسە"""),
                    const DividerSentences(),
                    SentencesRow(
