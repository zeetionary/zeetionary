                            """١. (ناو) ئۆفیسێکی حکومەتی بەریتانیا کە بەرپرسە لە سەپاندنی یاسا، کاری پۆلیس و زیندانەکان لە ئینگلاند و وێڵز، و ئەوەی کێ دەتوانێت بچێتە وڵاتەکەوە"""),
                    SentencesRow(
                      englishText:
