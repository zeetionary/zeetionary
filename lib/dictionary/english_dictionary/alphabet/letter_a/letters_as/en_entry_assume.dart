// DOPSUM: ENGLISH MEANING

class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    // Extract text from EnglishMeaningConst and store it in textToSpeak
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    // Update the state to reflect that TTS is in progress
    setState(() {
      isSpeaking = true;
    });
  }

  // Function to stop TTS
  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    // Update the state to reflect that TTS is stopped
    setState(() {
      isSpeaking = false;
    });
  }

  // Create an instance of EnglishMeaningConst with the desired text
  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Verb: assume (derived forms: assumed, assumes, assuming)
1. Take to be the case or to be true; accept without verification or proof (= presume, take for granted)
"I assume his train was late";
 
2. Take on titles, offices, duties, responsibilities (= adopt, take on, take over)
"When will the new President assume office?";
 
3. Take on a certain form, attribute, or aspect (= acquire, adopt, take on, take)
"She assumed strange manners"; "The gods assume human or animal form in these fables";
 
4. Take on as one's own the expenses or debts of another person (= bear, take over, accept)
"She agreed to assume the responsibility";
 
5. Occupy or take on (= take, strike, take up)
"He assumes the lotus position";
 
6. Take control of (without authority and possibly with force); take as one's right or possession (= usurp, seize, take over, arrogate)
"He assumed to himself the right to fill all positions in the town";
 
7. Make a pretence of (= simulate, sham, feign)
"She assumed indifference, even though she was seething with anger";
 
8. (Christianity) take up someone's soul into heaven
"This is the day when Mary was assumed into heaven"
 
9. Put clothing on one's body (= wear, put on, get into, don)
"The queen assumed the stately robes";
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const DividerDefinition(),
          // Using the EnglishButtonTTS class
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          // Speaker icon for American English
          englishMeaningConst,
        ],
      ),
    );
  }
}

