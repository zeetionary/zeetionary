                          """Nelson destroyed the Franco-Spanish fleet at the Battle of Trafalgar.""",
                      kurdishText:
                          """نێڵسن کەشتیگەکی فەرەنسی-ئیسپانی لە شەڕی ترافڵگار تێکشکاند.""",
                      onPressedBritish: () => speaksentence1("en-GB"),
                      onPressedAmerican: () => speaksentence1("en-US"),
                    ),
                    const DividerSentences(),
                    SentencesRow(
                      englishText: """The entire fleet was sunk.""",
                      kurdishText: """تەواوی کەشتیگەلەکە نوقمکرا.""",
                      onPressedBritish: () => speaksentence2("en-GB"),
                      onPressedAmerican: () => speaksentence2("en-US"),
                    ),
                    const DividerDefinition(),
                    const DefinitionKurdish(
                        text: """٢. (ناو) ژمارەیەک کەشتی کە پێکەوە ڕاودەکەن"""),
                    SentencesRow(
                      englishText: """The whaling fleet set sail at dawn.""",
                      kurdishText:
                          """دەستەکەشتییە نەھەنگ ڕاوکەرەکە لە بەرەبەیاندا دەستیان بە گەشت دەکرد.""",
                      onPressedBritish: () => speaksentence3("en-GB"),
                      onPressedAmerican: () => speaksentence3("en-US"),
                    ),
                    const DividerDefinition(),
                    const DefinitionKurdish(
                        text: """٣. (ناو) تەاوی کەشتی جەنگی وڵاتێک"""),
                    SentencesRow(
                      englishText:
                          """The fleet was mobilized and the country prepared for war.""",
                      kurdishText:
                          """ھێزی دەریایی خرایە حاڵەتی ئامادەباشی و وڵاتەکە ئامادەکاری بۆ جەنگ کرد.""",
                      onPressedBritish: () => speaksentence4("en-GB"),
                      onPressedAmerican: () => speaksentence4("en-US"),
                    ),
                    const DividerDefinition(),
                    const DefinitionKurdish(
                        text:
                            """٤. (ناو) ژمارەیەک فڕۆکە، پاس، تەکسی، ھتد ەکە پێکەوە گەشت دەکەن و یان ھی ھەمان دامەزراوەن"""),
                    SentencesRow(
                      englishText:
                          """The airline is doubling the size of its fleet.""",
                      kurdishText:
                          """ھێڵە ئاسمانییەکە ڕێژەی فڕۆکەکانی دوو ھێندە دەکات.""",
                      onPressedBritish: () => speaksentence5("en-GB"),
                      onPressedAmerican: () => speaksentence5("en-US"),
                    ),
                  ],
                ),
              ),
              Consumer(
                builder: (context, ref, child) {
                  if (filteredSentences.isEmpty) {
                    return const NoSentencesFromDatabase();
                  } else {
                    return ListView.builder(
                      itemCount: filteredSentences.length,
                      itemBuilder: (context, index) {
                        final sentence = filteredSentences[index];
                        final showDivider = filteredSentences.length > 1 &&
                            index != filteredSentences.length - 1;
                        return CustomSentenceWidget(
                          englishText: sentence['english'].toString(),
                          frenchText: sentence['french'].toString(),
                          keyword: keyword,
                          onPressedBritish: () => speakEnglish(
                            sentence['english'].toString(),
                            languageCode: "en-GB",
                          ),
                          onPressedAmerican: () => speakEnglish(
                            sentence['english'].toString(),
                            languageCode: "en-US",
                          ),
                          showDivider: showDivider,
                        );
                      },
                    );
                  }
                },
              ),
              const YouTubeScroller(
                children: [
                  YoutubeEmbeddedone(),
                  YoutubeEmbeddedtwo(),
                  YoutubeEmbeddedthree(),
                  YoutubeEmbeddedfour(),
                  YoutubeEmbeddedfive(),
                  // YoutubeEmbeddedsix(),
