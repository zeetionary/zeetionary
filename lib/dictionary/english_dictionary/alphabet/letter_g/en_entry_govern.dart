                      americanText: """IpaUS: /ˈɡʌvərn/""",
                      onPressedBritish: () => speakheadword("en-GB"),
                      onPressedAmerican: () => speakheadword("en-US"),
                    ),
                  ),
                ),
                automaticallyImplyLeading: false,
                bottom: const TabBar(
                  tabs: [
                    UkIconForTab(),
                    KurdIconForTab(),
                    SentencesIconForTab(),
                    VideoIconForTab(),
                  ],
                ),
              ),
            ];
          },
          body: TabBarView(
            children: [
              SingleChildScrollView(
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    const DividerDefinition(),
                    EnglishButtonTTS(
                      onBritishPressed: (languageCode) =>
                          startSpeaking(languageCode, englishMeaningConst),
                      onAmericanPressed: (languageCode) =>
                          startSpeaking(languageCode, englishMeaningConst),
                      onStopPressed: stopSpeaking,
                    ),
                    englishMeaningConst,
                  ],
                ),
              ),
              SingleChildScrollView(
                child: CustomColumnWidget(
                  children: [
                    const DividerDefinition(),
                    const KurdishVocabulary(text: """
کوردی: حکومەت کردن، حوکمڕانی کردن
"""),
// With short examples define "govern", please follow LX instructions
                    const DefinitionKurdish(
                        text:
                            """١. (کردار) بەڕێوەبردنی وڵاتێک یان خەڵکەکەی بەوەی بەرپرسی دانانی یاسا، ڕێکخستنی خزمەتگوزارییە گشتییەکان، ھتد بیت"""),
                    SentencesRow(
                      englishText:
                          """The country is governed by elected representatives of the people.""",
                      kurdishText:
                          """وڵاتەکە بەڕێوە دەبردرێت لەلایەن نوێنەرانی ھەڵبژێردراوی خەڵکی.""",
                      onPressedBritish: () => speaksentence1("en-GB"),
                      onPressedAmerican: () => speaksentence1("en-US"),
                    ),
                    const DividerSentences(),
                    SentencesRow(
                      englishText:
                          """He accused the opposition party of being unfit to govern.""",
                      kurdishText:
                          """پارتی ئۆپۆزسیۆنی بە ناشایستە بۆ حکومڕانی تۆمەتبار کرد.""",
                      onPressedBritish: () => speaksentence2("en-GB"),
                      onPressedAmerican: () => speaksentence2("en-US"),
                    ),
                    const DividerSentences(),
                    SentencesRow(
                      englishText:
                          """There were four parties governing in coalition.""",
                      kurdishText:
                          """چوار پارت بوون بە ھاوپەیمانی حکومڕانییان دەکرد.""",
                      onPressedBritish: () => speaksentence3("en-GB"),
                      onPressedAmerican: () => speaksentence3("en-US"),
                    ),
                    const DividerDefinition(),
                    const DefinitionKurdish(
                        text:
                            """٢. (کردار) کۆنترۆڵ کردن یان کاریگەری دانان لەسەر ئەوەی کەسێک/شتێک چۆن کار دەکات، ڕوودەدات، ھتد"""),
                    SentencesRow(
                      englishText: """Prices are governed by market demand.""",
                      kurdishText:
                          """نرخەکان لەلایەن خواستی بازاڕەوە دیاری دەکرێن.""",
                      onPressedBritish: () => speaksentence4("en-GB"),
                      onPressedAmerican: () => speaksentence4("en-US"),
                    ),
                    const DividerSentences(),
                    SentencesRow(
                      englishText:
                          """All his decisions have been entirely governed by self-interest.""",
                      kurdishText:
                          """ھەموو بڕیارەکانی کاریگەر بوونە بە بەرژەوەندیی کەسی.""",
                      onPressedBritish: () => speaksentence5("en-GB"),
                      onPressedAmerican: () => speaksentence5("en-US"),
                    ),
                  ],
                ),
              ),
              Consumer(
                builder: (context, ref, child) {
                  if (filteredSentences.isEmpty) {
                    return const NoSentencesFromDatabase();
                  } else {
                    return ListView.builder(
                      itemCount: filteredSentences.length,
                      itemBuilder: (context, index) {
                        final sentence = filteredSentences[index];
                        final showDivider = filteredSentences.length > 1 &&
                            index != filteredSentences.length - 1;
                        return CustomSentenceWidget(
                          englishText: sentence['english'].toString(),
                          frenchText: sentence['french'].toString(),
                          keyword: keyword,
                          onPressedBritish: () => speakEnglish(
                            sentence['english'].toString(),
                            languageCode: "en-GB",
                          ),
                          onPressedAmerican: () => speakEnglish(
                            sentence['english'].toString(),
                            languageCode: "en-US",
                          ),
                          showDivider: showDivider,
                        );
                      },
                    );
                  }
                },
              ),
              const YouTubeScroller(
                children: [
                  YoutubeEmbeddedone(),
                  YoutubeEmbeddedtwo(),
                  YoutubeEmbeddedthree(),
                  YoutubeEmbeddedfour(),
                  YoutubeEmbeddedfive(),
                  // YoutubeEmbeddedsix(),
                  // YoutubeEmbeddedseven(),
