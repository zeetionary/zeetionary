    const String videoIdtwo = '4DfCXjwQBRA';
    const double startSecondstwo = 0;
    const String videoIdthree = 'TijghKYYeAA';
    const double startSecondsthree = 664;
    const String videoIdfour = 'yRUUMTPhLH0';
    const double startSecondsfour = 3605;
    const String videoIdfive = 'dsCjRjA4O7Y';
    const double startSecondsfive = 1008;

    return DefaultTabController(
      length: 4,
      child: Scaffold(
        body: NestedScrollView(
          headerSliverBuilder: (context, innerBoxIsScrolled) {
            return [
              CustomSliverAppBar(
                flexibleSpace: FlexibleSpaceBar(
                  background: SingleChildScrollView(
                    child: EntryPageColumn(
                      word: """home page""",
                      // alsoEnglishWord: "also: home page",
                      britshText: """IpaUK: /ˈhəʊm peɪdʒ/""",
                      americanText: """IpaUS: /ˈhəʊm peɪdʒ/""",
                      onPressedBritish: () => speakheadword("en-GB"),
                      onPressedAmerican: () => speakheadword("en-US"),
                    ),
                  ),
                ),
                bottom: const CustomTabBarNew(
                  tabs: [
                    UkIconForTab(),
                    KurdIconForTab(),
                    SentencesIconForTab(),
                    VideoIconForTab(),
                  ],
                ),
              ),
            ];
          },
          body: TabBarView(
            children: [
              SingleChildScrollView(
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    EnglishButtonTTS(
                      onBritishPressed: (languageCode) =>
                          startSpeaking(languageCode, englishMeaningConst),
                      onAmericanPressed: (languageCode) =>
                          startSpeaking(languageCode, englishMeaningConst),
                      onStopPressed: stopSpeaking,
                    ),
                    englishMeaningConst,
                  ],
                ),
              ),
              SingleChildScrollView(
                child: CustomColumnWidget(
                  children: [
