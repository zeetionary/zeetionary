enum TtsState { playing }

class EnglishEntryhinder extends StatefulWidget {
  const EnglishEntryhinder({super.key});

  @override
  State<EnglishEntryhinder> createState() => _EnglishEntryhinderState();
}

class _EnglishEntryhinderState extends State<EnglishEntryhinder> {
  @override
  void initState() {
    super.initState();
    flutterTts = FlutterTts();
    flutterTts.setLanguage("en-GB");
    flutterTts.setLanguage("en-US");
    fetchSentences();
  }

  FlutterTts flutterTts = FlutterTts();

  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.setPitch(ttsPitch);
    await flutterTts.setSpeechRate(ttsSpeechRate);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Verb: hinder (Derived forms: hindering, hinders, hindered)
1. Be a hindrance or obstacle to (- impede, fetter [literary], entrammel [literary], trammel [literary])
"She is hindering the progress of our project";
 
2. Hinder or prevent the progress or accomplishment of (- obstruct, blockade, block, stymie, stymy [rare], embarrass)
"His brother hindered him at every turn";
 
3. Put at a disadvantage (- handicap, hamper)
"The brace I have to wear is hindering my movements";

- Adjective: hinder 
1. Located at or near the back of an animal (- back, hind)
"hinder legs"; "the hinder part of a carcass";
""",
  );

  final String keyword = "hinder";
  List<Map<String, dynamic>> filteredSentences = [];

  Future<void> fetchSentences() async {
    final sentences =
        await DatabaseUtils.instance.fetchFilteredSentences(keyword: keyword);
    setState(() {
      filteredSentences = sentences;
    });
  }

  void speakEnglish(String text, {String? languageCode}) async {
    await flutterTts.setLanguage(languageCode ?? "en-GB");
    await flutterTts.speak(text);
  }

  Future<void> speakheadword(String languageCode) async {
    await flutterTts.setLanguage(languageCode);
    await flutterTts.setPitch(ttsPitch);
    await flutterTts.setSpeechRate(ttsSpeechRate);
    await flutterTts.speak("""hinder""");
  }

  Future<void> speaksentence1(String languageCode) async {
    await flutterTts.setLanguage(languageCode);
    await flutterTts.setPitch(ttsPitch);
    await flutterTts.setSpeechRate(ttsSpeechRate);
    await flutterTts
        .speak("""Some teachers felt hindered by a lack of resources.""");
  }

  Future<void> speaksentence2(String languageCode) async {
    await flutterTts.setLanguage(languageCode);
    await flutterTts.setPitch(ttsPitch);
    await flutterTts.setSpeechRate(ttsSpeechRate);
    await flutterTts
        .speak("""An injury was hindering him from playing his best.""");
  }

  @override
  Widget build(BuildContext context) {
    const String videoIdend = 'H1KP4ztKK0A';
    const double startSecondsend = 1052;
    const String videoIdone = 'zhdBNVY55oM';
    const double startSecondsone = 316;
    const String videoIdtwo = 'kVOTPAxrrP4';
    const double startSecondstwo = 942;
    const String videoIdthree = 'vjDYfvPW4mA';
    const double startSecondsthree = 869;
    const String videoIdfour = '_rBO8neWw04';
    const double startSecondsfour = 1504;
    const String videoIdfive = '7bo3RaOEL8g';
    const double startSecondsfive = 271;

    return DefaultTabController(
      length: 4,
      child: Scaffold(
        appBar: const ZeetionaryAppbar(),
        body: NestedScrollView(
          headerSliverBuilder: (context, innerBoxIsScrolled) {
            return [
              CustomSliverAppBar(
                flexibleSpace: FlexibleSpaceBar(
                  background: SingleChildScrollView(
                    child: EntryPageColumn(
                      word: """hinder""",
                      // alsoEnglishWord: "also: hinder",
                      britshText: """IpaUK: /ˈhɪndə(r)/""",
                      americanText: """IpaUS: /ˈhɪndər/""",
                      onPressedBritish: () => speakheadword("en-GB"),
                      onPressedAmerican: () => speakheadword("en-US"),
                    ),
                  ),
                ),
                bottom: const CustomTabBarNew(
                  tabs: [
                    UkIconForTab(),
                    KurdIconForTab(),
                    SentencesIconForTab(),
                    VideoIconForTab(),
                  ],
                ),
              ),
            ];
          },
          body: TabBarView(
            children: [
              SingleChildScrollView(
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    EnglishButtonTTS(
                      onBritishPressed: (languageCode) =>
                          startSpeaking(languageCode, englishMeaningConst),
                      onAmericanPressed: (languageCode) =>
                          startSpeaking(languageCode, englishMeaningConst),
                      onStopPressed: stopSpeaking,
                    ),
                    englishMeaningConst,
                  ],
                ),
              ),
              SingleChildScrollView(
                child: CustomColumnWidget(
