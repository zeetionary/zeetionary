class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Verb: exhaust (derived forms: exhausts, exhausting, exhausted)
1. Wear out completely (= wash up, beat [informal], tucker [N. Amer, informal], tucker out [N. Amer, informal])
"This kind of work exhausts me";
 
2. Use (resources or materials) over time in order to function (= consume, eat up, use up, eat, deplete, run through, wipe out)
"We exhausted our savings";
 
3. Deplete, exhaust or weaken (= run down, play out, sap, tire)
"exhaust one's savings";
 
4. Use up the whole supply of
"We have exhausted the food supplies"
 
5. Eliminate (a substance) (= discharge, expel, eject, release)
"combustion products are exhausted in the engine";
 
6. Bring out, discuss or develop completely
"to exhaust a subject"

- Noun: exhaust (derived forms: exhausts)
1. Gases ejected from an engine as waste products (= exhaust fumes, fumes)
 
2. System consisting of the parts of an engine through which burned gases or steam are discharged (= exhaust system)
 
3. [Brit] A pipe carrying fumes from the muffler to the rear of a car (= tailpipe [N. Amer], exhaust pipe [Brit])
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

