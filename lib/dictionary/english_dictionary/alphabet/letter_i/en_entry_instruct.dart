    text: """
- Verb: instruct (Derived forms: instructs, instructed, instructing)
1. Impart skills or knowledge to (- teach, learn)
"He instructed me in building a boat";
 
2. Give instructions or directions for some task
"She instructed the students to work on their pronunciation"
 
3. Make aware of (- apprise)
""",
  );

  final String keyword = "instruct";
  List<Map<String, dynamic>> filteredSentences = [];

  Future<void> fetchSentences() async {
    final sentences =
        await DatabaseUtils.instance.fetchFilteredSentences(keyword: keyword);
    setState(() {
      filteredSentences = sentences;
    });
  }

  void speakEnglish(String text, {String? languageCode}) async {
    await flutterTts.setLanguage(languageCode ?? "en-GB");
    await flutterTts.speak(text);
  }

