class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Noun: factor (derived forms: factors)
1. Anything that contributes causally to a result
"a number of factors determined the outcome"
 
2. An abstract part of something (= component, constituent, element, ingredient)
"a key factor in her success";
 
2. One of two or more integers that can be exactly divided into another integer (= divisor)
"what are the 4 factors of 6?";
 
3. A businessman who buys or sells for another in exchange for a commission (= agent, broker)
 
4. Any of the numbers (or symbols) that form a product when multiplied together
 
5. An independent variable in statistics
 
6. (genetics) a segment of DNA that is involved in producing a polypeptide chain; it can include regions preceding and following the coding DNA as well as introns between the exons; it is considered a unit of heredity (= gene, cistron)
"genes were formerly called factors"; "cistrons were formerly called factors";

- Verb: factor (derived forms: factors, factoring, factored)
1. (arithmetic) resolve into factors (= factor in, factor out)
"a quantum computer can factor the number 15";
 
2. Be a contributing factor
"make things factor into a company's profitability"
 
3. Consider as relevant when making a decision (= factor in, factor out)
"You must factor in the recent developments";
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

