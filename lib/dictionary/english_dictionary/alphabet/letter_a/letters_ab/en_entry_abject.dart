                      englishText:
                          "the most abject slaves joined in the revolt",
                      kurdishText:
                          "ئەو کۆیلانەی لە خراپترین بارودۆخدا بوون پەیوەندییان کرد بە شۆڕشەکەوە.",
                      onPressedBritish: () => speakab61("en-GB"),
