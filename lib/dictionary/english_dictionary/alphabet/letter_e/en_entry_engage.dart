class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Verb: engage (derived forms: engaged, engaging, engages)
1. Carry out or participate in an activity; be involved in (= prosecute, pursue)
"They engaged in a discussion";
 
2. Consume all of one's attention or time (= absorb, engross, occupy)
"Her interest in butterflies engages her completely";
 
3. Start in a job or pay to do a particular piece of work (= hire, employ)
"They engaged two new secretaries in the department";
 
4. Ask to represent; of legal counsel
 
5. Give to in marriage (= betroth [archaic], affiance [literary], plight [archaic])
 
6. Get caught
"make sure the gear is engaged"
 
7. Carry on (wars, battles, or campaigns) (= wage)
"Napoleon and Hitler engaged war against all of Europe";
 
8. Hire for work or assistance (= enlist)
"engage aid, help, services, or support";
 
9. Engage for service under a term of contract (= lease, rent, hire, charter, take)
"Let's engage a car";
 
10. Keep engaged (= mesh, lock, operate)
"engaged the gears";
 
11. (military) start armed combat with
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const DividerDefinition(),
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

