class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

// Function to stop TTS
  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Verb: differentiate (derived forms: differentiated, differentiates, differentiating)
1. Mark as different (= distinguish, separate, secern [rare], secernate, severalize, severalise [Brit], tell, tell apart)
"We differentiate several kinds of maple";
 
2. Be a distinctive feature, attribute, or trait; sometimes in a very positive sense (= distinguish, mark)
"His modesty differentiates him from his peers";
 
3. (mathematics) calculate a derivative; take the derivative
 
4. Become different during development
"cells differentiate"
 
5. Evolve so as to lead to a new species or develop in a way most suited to the environment (= speciate, specialize, specialise [Brit])
 
6. Become distinct and acquire a different character
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

