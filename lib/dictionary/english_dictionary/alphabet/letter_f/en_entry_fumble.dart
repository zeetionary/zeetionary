                    const DefinitionKurdish(
                        text:
                            """١. (کردار) بەکارھێنانی دەست بە شێوەیەک کە جێگیر یان بە وریایی نییە کە شتێک دەکەیت یان بۆ شتێک دەگەڕێیت"""),
                    SentencesRow(
                      englishText:
                          """She fumbled in her pocket for a handkerchief.""",
                      kurdishText: """باخەڵی گەڕا بۆ دەستەسڕێک.""",
                      onPressedBritish: () => speaksentence1("en-GB"),
                      onPressedAmerican: () => speaksentence1("en-US"),
                    ),
                    const DividerSentences(),
                    SentencesRow(
                      englishText:
                          """He fumbled with the buttons on his shirt.""",
                      kurdishText:
                          """بە دەستەپاچەیی دووگمەکانی کراسەکەی دەبەست.""",
                      englishNote:
                          """This means he clumsily or awkwardly handled the buttons on his shirt, possibly struggling to fasten or unfasten them.""",
                      onPressedBritish: () => speaksentence2("en-GB"),
                      onPressedAmerican: () => speaksentence2("en-US"),
                    ),
                    const DividerDefinition(),
                    const DefinitionKurdish(
                        text:
                            """٢. (کردار) ھەبوونی کێشەی ئەوەی بە ڕوونی قسە بکەیت یان وشەی گونجاو بەکاربھێنیت"""),
                    SentencesRow(
                      englishText:
                          """During the interview, she fumbled helplessly for words.""",
                      kurdishText:
                          """لە چاوپێکەوتنەکەدا، بێ توانایانە تەتەڵەی بوو بۆ وشي.""",
                      onPressedBritish: () => speaksentence3("en-GB"),
                      onPressedAmerican: () => speaksentence3("en-US"),
                    ),
                    const DividerDefinition(),
                    const DefinitionKurdish(
                        text:
                            """٣. (ناو) بەکارھێنانی دەست بە لەرزۆکی و نا وریایی"""),
                    SentencesRow(
                      englishText:
                          """After a brief fumble in her handbag, she produced a clean handkerchief.""",
                      kurdishText:
                          """لە دوای کەمێک لە دەستکردن بە جانتاکەیدا، دەستەسڕێکی پاکی دەرھێنا.""",
                      onPressedBritish: () => speaksentence4("en-GB"),
                      onPressedAmerican: () => speaksentence4("en-US"),
                    ),
                  ],
                ),
              ),
              Consumer(
                builder: (context, ref, child) {
                  if (filteredSentences.isEmpty) {
                    return const NoSentencesFromDatabase();
                  } else {
                    return ListView.builder(
                      itemCount: filteredSentences.length,
                      itemBuilder: (context, index) {
                        final sentence = filteredSentences[index];
                        final showDivider = filteredSentences.length > 1 &&
                            index != filteredSentences.length - 1;
                        return CustomSentenceWidget(
                          englishText: sentence['english'].toString(),
                          frenchText: sentence['french'].toString(),
                          keyword: keyword,
                          onPressedBritish: () => speakEnglish(
                            sentence['english'].toString(),
                            languageCode: "en-GB",
                          ),
                          onPressedAmerican: () => speakEnglish(
                            sentence['english'].toString(),
                            languageCode: "en-US",
                          ),
                          showDivider: showDivider,
                        );
                      },
                    );
                  }
                },
              ),
              const YouTubeScroller(
                children: [
                  YoutubeEmbeddedone(),
                  YoutubeEmbeddedtwo(),
                  YoutubeEmbeddedthree(),
                  YoutubeEmbeddedfour(),
                  YoutubeEmbeddedfive(),
                  // YoutubeEmbeddedsix(),
                  // YoutubeEmbeddedseven(),
                  // YoutubeEmbeddedeight(),
