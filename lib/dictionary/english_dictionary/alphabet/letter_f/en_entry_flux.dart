class EnglishEntryflux extends StatefulWidget {
  const EnglishEntryflux({super.key});

  @override
  State<EnglishEntryflux> createState() => _EnglishEntryfluxState();
}

class _EnglishEntryfluxState extends State<EnglishEntryflux> {
  @override
  void initState() {
    super.initState();
    flutterTts = FlutterTts();
    flutterTts.setLanguage("en-GB");
    flutterTts.setLanguage("en-US");
    fetchSentences();
  }

  FlutterTts flutterTts = FlutterTts();

  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Noun: flux (derived forms: fluxes)
1. The rate of flow of energy or particles across a given surface
 
2. A flow or discharge (= fluxion [archaic])
 
3. A substance added to molten metals to bond with impurities that can then be readily removed
 
4. Excessive discharge of liquid from a cavity or organ (as in watery diarrhoea)
 
5. A state of uncertainty about what should be done (usually following some important event) preceding the establishment of a new direction of action (= state of flux)
"the flux following the death of the emperor";
 
6. The lines of force surrounding a permanent magnet or a moving charged particle (= magnetic field, magnetic flux)
 
7. (physics) the number of changes in energy flow across a given surface per unit area (= flux density)
 
8. In constant change
"his opinions are in flux"; "the newness and flux of the computer industry"

- Verb: flux (derived forms: fluxes, fluxing, fluxed)
1. Move or progress freely as if in a stream (= flow)
"The crowd fluxed out of the stadium";
 
2. (physics) become liquid or fluid when heated (= liquefy, liquify)
 
3. Add together different elements (= blend, mix, conflate, commingle, immix, fuse, coalesce, meld, combine, merge)
"The colours flux well";

""",
  );

  final String keyword = "flux";
  List<Map<String, dynamic>> filteredSentences = [];

  Future<void> fetchSentences() async {
    final sentences =
        await DatabaseUtils.instance.fetchFilteredSentences(keyword: keyword);
    setState(() {
      filteredSentences = sentences;
    });
  }

  void speakEnglish(String text, {String? languageCode}) async {
    await flutterTts.setLanguage(languageCode ?? "en-GB");
    await flutterTts.speak(text);
  }

  Future<void> speakheadword(String languageCode) async {
    await flutterTts.setLanguage(languageCode);
    await flutterTts.setPitch(ttsPitch);
    await flutterTts.setSpeechRate(ttsSpeechRate);
    await flutterTts.speak("""flux""");
  }

  Future<void> speaksentence1(String languageCode) async {
    await flutterTts.setLanguage(languageCode);
    await flutterTts.setPitch(ttsPitch);
    await flutterTts.setSpeechRate(ttsSpeechRate);
    await flutterTts.speak("""Our society is in a state of flux.""");
  }

  Future<void> speaksentence2(String languageCode) async {
    await flutterTts.setLanguage(languageCode);

    await flutterTts
        .speak("""There is a rapid flux of ions across the membrane.""");
  }

  Future<void> speaksentence3(String languageCode) async {
    await flutterTts.setLanguage(languageCode);
    await flutterTts.setPitch(ttsPitch);
    await flutterTts.setSpeechRate(ttsSpeechRate);
    await flutterTts.speak(
        """Researchers analyze the flux of neutrinos between the two vats.""");
  }

  @override
  Widget build(BuildContext context) {
    return DefaultTabController(
      length: 4,
      child: Scaffold(
        appBar: const ZeetionaryAppbar(),
        body: NestedScrollView(
          headerSliverBuilder: (context, innerBoxIsScrolled) {
            return [
              CustomSliverAppBar(
                flexibleSpace: FlexibleSpaceBar(
                  background: SingleChildScrollView(
                    child: EntryPageColumn(
                      word: """flux""",
                      // alsoEnglishWord: "also: flux",
                      britshText: """IpaUK: /flʌks/""",
                      americanText: """IpaUS: /flʌks/""",
                      onPressedBritish: () => speakheadword("en-GB"),
                      onPressedAmerican: () => speakheadword("en-US"),
                    ),
                  ),
                ),
                bottom: const CustomTabBarNew(
                  tabs: [
                    UkIconForTab(),
                    KurdIconForTab(),
                    SentencesIconForTab(),
                    VideoIconForTab(),
                  ],
                ),
              ),
            ];
          },
          body: TabBarView(
            children: [
              SingleChildScrollView(
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    EnglishButtonTTS(
                      onBritishPressed: (languageCode) =>
                          startSpeaking(languageCode, englishMeaningConst),
                      onAmericanPressed: (languageCode) =>
                          startSpeaking(languageCode, englishMeaningConst),
                      onStopPressed: stopSpeaking,
                    ),
                    englishMeaningConst,
                  ],
                ),
              ),
              SingleChildScrollView(
                child: CustomColumnWidget(
                  children: [
                    const KurdishVocabulary(text: """
کوردی: ڕۆیشتن، ڕۆیین، ڕەوت، ڕەوندەیی، ڕەوان،	خێرایی ڕەوت یان ڕۆیشتن،	گۆڕانی بەردەوام، ناجێگیری، دۆخی ناچەسپیو،	(پزشکی) ڕژان، دەردان،	زگچوون، ڕەوانی، زگەشۆرە،	دەرپەڕین، ڕەوندەیی، ڕۆیین، ڕەوت، لێشاو، شلی، شلەیی، ڕژۆکی، شلکەرەوە، شلەک، مەتریاڵێ کە یارمەتی شل‌بوونەوەی کانزاکان دەکا، توێنە"""),
// With short examples define "flux", please follow LX instructions
                    const DefinitionKurdish(
                        text: """١. (ناو) جووڵە و گۆڕانی بەردەوام"""),
                    SentencesRow(
                      englishText: """Our society is in a state of flux.""",
                      kurdishText: """کۆمەڵگاکەمان لە دۆخی گۆڕاندایە.""",
                      onPressedBritish: () => speaksentence1("en-GB"),
                      onPressedAmerican: () => speaksentence1("en-US"),
                    ),
                    const DividerDefinition(),
                    const DefinitionKurdish(
                        text:
                            """٢. (ناو) جووڵەی بەردەوامی شتێک، بۆ نموونە تەنۆلکەکان"""),
                    SentencesRow(
                      englishText:
                          """There is a rapid flux of ions across the membrane.""",
                      kurdishText:
                          """دەرپەڕینی زۆری ئایۆن ھەیە بە مێمبرەینەکەدا.""",
                      onPressedBritish: () => speaksentence2("en-GB"),
                      onPressedAmerican: () => speaksentence2("en-US"),
                    ),
                    const DividerSentences(),
                    SentencesRow(
                      englishText:
                          """Researchers analyze the flux of neutrinos between the two vats.""",
                      kurdishText:
                          """لێکۆڵەران شیکردنەوە دەکەن بۆ دەرپەڕینی نیوترینۆی نێوان دوو دەفرەکە.""",
                      onPressedBritish: () => speaksentence3("en-GB"),
                      onPressedAmerican: () => speaksentence3("en-US"),
                    ),
                  ],
                ),
              ),
              Consumer(
                builder: (context, ref, child) {
                  if (filteredSentences.isEmpty) {
                    return const NoSentencesFromDatabase();
                  } else {
                    return ListView.builder(
                      itemCount: filteredSentences.length,
                      itemBuilder: (context, index) {
                        final sentence = filteredSentences[index];
                        final showDivider = filteredSentences.length > 1 &&
                            index != filteredSentences.length - 1;
                        return CustomSentenceWidget(
                          englishText: sentence['english'].toString(),
                          frenchText: sentence['french'].toString(),
                          keyword: keyword,
                          onPressedBritish: () => speakEnglish(
                            sentence['english'].toString(),
                            languageCode: "en-GB",
                          ),
                          onPressedAmerican: () => speakEnglish(
                            sentence['english'].toString(),
                            languageCode: "en-US",
                          ),
                          showDivider: showDivider,
                        );
                      },
                    );
                  }
                },
              ),
              const YouTubeScroller(
                children: [
                  YoutubeEmbeddedone(),
                  YoutubeEmbeddedtwo(),
                  YoutubeEmbeddedthree(),
                  YoutubeEmbeddedfour(),
                  YoutubeEmbeddedfive(),
                  // YoutubeEmbeddedsix(),
                  // YoutubeEmbeddedseven(),
                  // YoutubeEmbeddedeight(),
                  // YoutubeEmbeddednine(),
                  // YoutubeEmbeddedten(),
                  // YoutubeEmbeddedeleven(),
                  // YoutubeEmbeddedtwelve(),
                  // YoutubeEmbeddedthirteen(),
                  // YoutubeEmbeddeddfourteen(),
                  // YoutubeEmbeddedfifteen(),
                  // YoutubeEmbeddeddsixteen(),
                  // YoutubeEmbeddeddseventeen(),
                  // YoutubeEmbeddeddeighteen(),
                  // YoutubeEmbeddeddnineteen(),
                  // YoutubeEmbeddedtwenty(),
                  // YoutubeEmbeddedmulti(),
                  YoutubeEmbeddedend(),
                ],
              ),
            ],
          ),
        ),
      ),
    );
  }
}

