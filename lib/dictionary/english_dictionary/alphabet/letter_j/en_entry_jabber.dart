                    SentencesRow(
                      englishText: """What is he jabbering about now?""",
                      kurdishText: """ئەو مڕەمڕە لە چییە؟""",
                      onPressedBritish: () => speaksentence1("en-GB"),
                      onPressedAmerican: () => speaksentence1("en-US"),
