    text: """
- Noun: flock (derived forms: flocks)
1. A church congregation guided by a pastor
 
2. A group of birds
 
3. (often followed by 'of') a large number, amount or extent (= batch, deal, good deal, great deal, hatful, heap, lot, mass, mess, mickle [archaic], mint, mountain, muckle, passel [US], peck, pile [informal], plenty, pot, quite a little, raft, sight, slew, spate, stack, tidy sum, wad, bunch [informal])
"a flock of letters";
 
4. An orderly crowd (= troop)
"a flock of children";
 
5. A group of sheep or goats (= fold)
 
6. Coarse tufts of wool or cotton used in bedding

- Verb: flock (derived forms: flocked, flocking, flocks)
1. Move as a crowd or in a group
"Tourists flocked to the shrine where the statue was said to have shed tears"
 
2. Come together as in a cluster or flock (= cluster, constellate, clump)
""",
  );

