    text: """
- Verb: harden (Derived forms: hardened, hardens, hardening)
1. Become hard or harder (- indurate)
"The wax hardened";
 
2. Make hard or harder (- indurate)
"The cold hardened the butter";
 
3. Harden by reheating and cooling in oil (- temper)
"harden steel";
 
4. Make healthy (- season)
"This trip will harden even the hardiest traveller";
 
5. Cause to accept or become hardened to; habituate (- inure, indurate, enure)
"He was hardened to the cold";
""",
  );

