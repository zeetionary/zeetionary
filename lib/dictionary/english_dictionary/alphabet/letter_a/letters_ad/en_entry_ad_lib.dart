class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  // Function to stop TTS
  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    // Update the state to reflect that TTS is stopped
    setState(() {
      isSpeaking = false;
    });
  }

  // Create an instance of EnglishMeaningConst with the desired text
  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Adjective: ad-lib (derived forms: ad-libbed, ad-libbing, ad-libs)
1. With little or no preparation or forethought (extemporaneous, extemporary, extempore, impromptu, offhand, offhanded, off-the-cuff, unrehearsed)
"his ad-lib comments showed poor judgment";
 
2. Said or done without having been planned or written in advance (= spontaneous, unwritten)
"he made a few ad-lib remarks";

- Noun: ad-lib  ad lib
1. Remark made spontaneously without prior preparation
"his ad-libs got him in trouble with the politicians"

- Verb: ad-lib (ad-libbed,ad-libbing)  ad lib
1. Perform without preparation (= improvise, improvize [non-standard], extemporize, extemporise [Brit], wing it [informal], wing [informal])
"he ad-libbed a speech at the wedding";

- Adverb: ad lib  ad lib
1. Without advance preparation (= ad libitum, spontaneously, impromptu)
"he spoke ad lib";
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const DividerDefinition(),

          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          // Speaker icon for American English
          englishMeaningConst,
        ],
      ),
    );
  }
}

