class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  // Function to stop TTS
  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    // Update the state to reflect that TTS is stopped
    setState(() {
      isSpeaking = false;
    });
  }

  // Create an instance of EnglishMeaningConst with the desired text
  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Noun: business (derived forms: businesses)
1. A commercial or industrial enterprise and the people who constitute it (= concern, business concern, business organization, business organisation [Brit])
"he bought his brother's business"; "a small mom-and-pop business"; "a racially integrated business concern"; "he bought his brother's business organization";
 
2. The activity of providing goods and services involving financial and commercial and industrial aspects (= commercial enterprise, business enterprise)
"computers are now widely used in business";
 
3. What someone normally does to earn money; a person's profession, work, or trade (= occupation, job, line of work, line)
"he's not in my line of business";

4. A rightful concern or responsibility
"it's none of your business"; "mind your own business"
 
5. An immediate objective
"gossip was the main business of the evening"
 
6. The volume of commercial activity
"business is good today"; "show me where the business was today"
 
7. The commercial part of the economy; business concerns collectively (= business sector)
"Government and business could not agree";

8. Customers collectively (= clientele, patronage)
"they have an upper class business";
 
9. Incidental activity performed by an actor for dramatic effect (= stage business, byplay)
"his business with the cane was hilarious";
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

