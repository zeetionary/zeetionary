    text: """
- Noun: hazard (Derived forms: hazards)
1. A source of danger; a possibility of incurring loss or misfortune (- jeopardy, peril, risk, endangerment)
"drinking alcohol is a health hazard";
 
2. An unknown and unpredictable phenomenon that causes an event to result one way rather than another (- luck, fortune, chance)
 
3. An obstacle on a golf course

- Verb: hazard (Derived forms: hazards, hazarding, hazarded)
1. Put forward, of a guess, in spite of possible refutation (- guess, venture, pretend)
 
2. Put at risk (- venture, adventure, stake, jeopardize, jeopardise [Brit])
"I will hazard my good reputation for this";
 
3. Take a risk in the hope of a favourable outcome (- gamble, chance, risk, take chances, adventure, run a risk, take a chance)
"When you buy these stocks you are hazarding";
""",
  );

  final String keyword = "hazard";
  List<Map<String, dynamic>> filteredSentences = [];

