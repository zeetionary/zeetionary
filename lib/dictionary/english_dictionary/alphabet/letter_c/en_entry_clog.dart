class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  // Function to stop TTS
  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    // Update the state to reflect that TTS is stopped
    setState(() {
      isSpeaking = false;
    });
  }

  // Create an instance of EnglishMeaningConst with the desired text
  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Verb: clog (derived forms: clogged, clogs, clogging)
1. Become or cause to become obstructed (= choke off, clog up, back up, congest, choke, foul)
"The leaves clog our drains in the Fall";
 
2. Impede the motion of, as with a chain or a burden
"horses were clogged until they were tamed"
 
3. Impede with a clog or as if with a clog (= constipate)
"The market is being clogged by these operations";
 
4. Coalesce or unite in a mass (= clot)
 
5. Fill to excess so that function is impaired (= overload)
"Fear clogged her mind"; "The story was clogged with too many details";
 
6. (dancing) dance a clog dance

- Noun: clog (derived forms: clogs)
1. Footwear usually with wooden soles (= geta, patten, sabot)
 
2. Any object that acts as a hindrance or obstruction
 
3. A dance performed while wearing shoes with wooden soles; has heavy stamping steps (= clog dance, clog dancing, clogging [N. Amer])
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const DividerDefinition(),

          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          // Speaker icon for American English
          englishMeaningConst,
        ],
      ),
    );
  }
}

