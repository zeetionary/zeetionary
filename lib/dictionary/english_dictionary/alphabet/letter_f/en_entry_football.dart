1. Any of various games played with a ball (round or oval) in which two teams try to kick or carry or propel the ball into each other's goal (= football game)
 
2. The inflated oblong ball used in playing American football
 
3. [Brit] A football game in which two teams of 11 players try to kick or head a ball into the opponents' goal (= soccer, association football, beautiful game [Brit, informal])
 
4. [Brit] An inflated ball used in playing soccer (= soccer ball)
""",
  );

