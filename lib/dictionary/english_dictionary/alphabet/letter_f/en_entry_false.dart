class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Adjective: false (derived forms: falser, falsest)
1. Not in accordance with the fact, reality or actuality
"gave false testimony under oath"; "false tales of bravery"
 
2. Arising from error (= mistaken)
"a false assumption";
 
3. Erroneous and usually accidental
"a false start"; "a false alarm"
 
4. Deliberately deceptive
"false pretences"
 
5. Inappropriate to reality or facts (= delusive)
"false hopes";
 
6. Not genuine or real; being an imitation of the genuine article (= fake, faux, imitation, simulated)
"false teeth";
 
7. Designed to deceive
"a suitcase with a false bottom"
 
8. Inaccurate in pitch (= off-key, sour)
"a false note";
 
9. Adopted in order to deceive (= assumed, fictitious, fictive, pretended, put on, sham)
 
10. (used especially of persons) not dependable in devotion or affection; unfaithful (= untrue)
"a false friend";

- Adverb: false 
1. In a disloyal and faithless manner (= faithlessly, traitorously, treacherously, treasonably)
"his wife played him false";
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const DividerDefinition(),
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

