  FlutterTts flutterTts = FlutterTts();

  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.setPitch(ttsPitch);
    await flutterTts.setSpeechRate(ttsSpeechRate);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Noun: harbour (Derived forms: harbours)
Usage: Brit, Cdn (US: harbor)
1. A sheltered port where ships can take on or discharge cargo (- seaport, haven, harbor [N. Amer])
 
2. [Brit] A sheltered area of coast where ships can dock or anchor safely (- harbor [US], port)
 
3. A place of refuge and comfort and security (- harbor [N. Amer])

- Verb: harbour (Derived forms: harbours, harboured, harbouring)
Usage: Brit, Cdn (US: harbor)
1. Maintain (a theory, thoughts, or feelings) (- harbor [N. Amer], hold, entertain, nurse, bear)
"harbour a resentment";
 
2. Secretly shelter (as of fugitives or criminals) (- harbor [N. Amer])
 
3. Keep in one's possession; of animals (- harbor [N. Amer])
 
4. Hold back a thought or feeling about (- harbor [N. Amer], shield)
"She is harbouring a grudge against him";
""",
  );

  final String keyword = "harbour";
  List<Map<String, dynamic>> filteredSentences = [];

