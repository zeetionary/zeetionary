                          """My biggest frustration was not having enough time.""",
                      kurdishText:
                          """گەورەترین بێزاریم ئەوە بوو کاتم زۆر نەبوو.""",
                      onPressedBritish: () => speaksentence7("en-GB"),
                      onPressedAmerican: () => speaksentence7("en-US"),
                    ),
                  ],
                ),
