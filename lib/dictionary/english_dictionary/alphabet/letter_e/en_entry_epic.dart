class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Noun: epic (derived forms: epics)
1. A long narrative poem telling of a hero's deeds (= epic poem, heroic poem, epos)
 
2. A long story or event
"a war epic"

- Adjective: epic
1. Very imposing or impressive; surpassing the ordinary (especially in size or scale) (= heroic, larger-than-life)
"an epic voyage";
 
2. Constituting or having to do with or suggestive of a literary epic (= epical)
"epic tradition";
 
3. [slang] Extraordinarily good or great; used especially as an intensifier (= fantastic, grand, howling [informal], marvelous [US], marvellous [Brit, Cdn], rattling [informal], terrific, tremendous, wonderful, wondrous, phenomenal, magnificent, heavenly [informal])
 
4. [slang] Excellent and enjoyable or memorable (= mega [slang])
"we had an epic time";
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const DividerDefinition(),
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

