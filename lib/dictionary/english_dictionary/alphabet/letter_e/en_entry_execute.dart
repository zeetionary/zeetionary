class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Verb: execute (derived forms: executed, executing, executes)
1. Kill as a means of socially sanctioned punishment (= put to death)
"In some states, criminals are executed";
 
2. Murder in a planned fashion
"The Mafioso who collaborated with the police was executed"
 
3. Cause to happen; complete successfully (= carry through, accomplish, carry out, action, fulfill [N. Amer], fulfil [Brit, Cdn])
"execute the decision of the people";
 
4. Carry out the legalities of
"execute a will or a deed"
 
5. Carry out a process or program, as on a computer or a machine (= run)
"the computer executed the instruction";
 
6. Carry out an action (= perform, do)
"the skater executed a triple pirouette";
 
7. Sign in the presence of witnesses
"The President executed the treaty"
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const DividerDefinition(),
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

