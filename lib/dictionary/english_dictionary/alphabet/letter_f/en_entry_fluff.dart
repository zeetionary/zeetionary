- Noun: fluff (derived forms: fluffs)
1. Any light downy material
 
2. Something of little value or significance (= bagatelle, frippery, frivolity)
"mere rhetorical fluff";
 
3. [informal] A blunder (especially an actor's forgetting the lines)

- Verb: fluff (derived forms: fluffed, fluffing, fluffs)
1. [informal] Make a mess of, destroy or ruin (= botch, bodge [Brit, informal], bumble, fumble, botch up, muff [informal], blow, flub [N. Amer, informal], screw up [informal], spoil, muck up [informal], bungle, bobble [N. Amer], mishandle, louse up [informal], foul up, mess up, butcher, goof up [informal], cock up [Brit, informal], balls up [informal], make a hash of [informal])
"I fluffed the dinner and we had to eat out";
 
2. Erect or fluff up (= ruffle)
"the bird fluffed its feathers";
 
3. Ruffle (one's hair) by combing the ends towards the scalp, for a full effect (= tease)
""",
