  FlutterTts flutterTts = FlutterTts();

  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    setState(() {
      isSpeaking = false;
    });
  }

  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Noun: fiddle (derived forms: fiddles)
1. A violin used to play folk music
 
2. [informal] Bowed stringed instrument that is the highest member of the violin family; this  instrument has four strings and a hollow body and an unfretted fingerboard and is played with a bow (= violin)

3. [Brit, informal] Intentional deception resulting in injury to another person (= fraud)
 
4. [Brit, informal] Something awkward to do or use, requiring a degree of manual dexterity

- Verb: fiddle (derived forms: fiddling, fiddled, fiddles)
1. Manipulate manually or in one's mind or imagination (= toy, diddle [informal], play)
"Don't fiddle with the screws";
 
2. [informal] Play around with or alter or falsify, usually secretively or dishonestly (= tamper, monkey)
"the reporter fiddled with the facts";
 
3. [informal] Commit fraud and steal from one's employer
"We found out that she had been fiddling for years"
 
4. [informal] (music) play the violin or fiddle
"Zuckerman fiddled that song very nicely"
 
5. Try to fix or mend (= tinker)
"She always fiddles with her van on the weekend";
 
6. Avoid (one's assigned duties) (= shirk, shrink from, goldbrick [N. Amer, informal])
"The derelict soldier fiddled his duties";
""",
  );

  final String keyword = "fiddle";
  List<Map<String, dynamic>> filteredSentences = [];

  Future<void> fetchSentences() async {
    final sentences =
        await DatabaseUtils.instance.fetchFilteredSentences(keyword: keyword);
    setState(() {
      filteredSentences = sentences;
    });
  }

  void speakEnglish(String text, {String? languageCode}) async {
    await flutterTts.setLanguage(languageCode ?? "en-GB");
    await flutterTts.speak(text);
  }

  Future<void> speakheadword(String languageCode) async {
    await flutterTts.setLanguage(languageCode);
    await flutterTts.setPitch(ttsPitch);
    await flutterTts.setSpeechRate(ttsSpeechRate);
    await flutterTts.speak("""fiddle""");
  }

  Future<void> speaksentence1(String languageCode) async {
    await flutterTts.setLanguage(languageCode);

    await flutterTts
        .speak("""He was fiddling with his keys while he talked to me.""");
  }

  Future<void> speaksentence2(String languageCode) async {
    await flutterTts.setLanguage(languageCode);

    await flutterTts
        .speak("""She fiddled the books while working as an accountant.""");
  }

  Future<void> speaksentence3(String languageCode) async {
    await flutterTts.setLanguage(languageCode);
    await flutterTts.setPitch(ttsPitch);
    await flutterTts.setSpeechRate(ttsSpeechRate);
    await flutterTts.speak("""Everyone suspected they were on the fiddle.""");
  }

  Future<void> speaksentence4(String languageCode) async {
