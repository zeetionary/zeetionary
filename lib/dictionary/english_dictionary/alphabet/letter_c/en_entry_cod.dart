class EnglishMeaning extends StatefulWidget {
  const EnglishMeaning({super.key});

  @override
  State<EnglishMeaning> createState() => _EnglishMeaningState();
}

class _EnglishMeaningState extends State<EnglishMeaning> {
  FlutterTts flutterTts = FlutterTts();
  bool isSpeaking = false;

  Future<void> startSpeaking(
      String languageCode, EnglishMeaningConst englishMeaningConst) async {
    String textToSpeak = """
${englishMeaningConst.text}
""";

    await flutterTts.setLanguage(languageCode);
    await flutterTts.speak(textToSpeak);

    setState(() {
      isSpeaking = true;
    });
  }

  // Function to stop TTS
  Future<void> stopSpeaking() async {
    await flutterTts.stop();

    // Update the state to reflect that TTS is stopped
    setState(() {
      isSpeaking = false;
    });
  }

  // Create an instance of EnglishMeaningConst with the desired text
  final EnglishMeaningConst englishMeaningConst = const EnglishMeaningConst(
    text: """
- Noun: cod (derived forms: cod)
1. Major food fish of Arctic and cold-temperate waters (= codfish)
 
2. Lean white flesh of important North Atlantic food fish; usually baked or poached (= codfish)

- Adjective: cod 
Usage: Brit, informal
1. Fraudulent; having a misleading appearance (= bogus, fake, phony [N. Amer], phoney)

- Verb: cod (derived forms: cods, codded, codding)
Usage: Brit, informal
1. Fool or hoax (gull, dupe, slang [archaic, informal], befool [archaic], fool, put on, take in, put one over [informal], put one across [informal])
"The immigrant was codded because he trusted everyone";
 
2. Deliberately provoke by mocking or poking fun, often in an aggressive manner (= tease, razz [N. Amer, informal], rag, bait, taunt, twit [informal], rally, ride [N. Amer, informal], wind up [Brit, informal])
"The children codded the new teacher";

- Adverb: C.O.D.
1. Collecting the charges upon delivery (= COD, cash on delivery)
"mail a package C.O.D.";

- Adjective: COD
1. Payable by the recipient on delivery (= collect)
"a COD parcel";
""",
  );

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const DividerDefinition(),
          EnglishButtonTTS(
            onBritishPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onAmericanPressed: (languageCode) =>
                startSpeaking(languageCode, englishMeaningConst),
            onStopPressed: stopSpeaking,
          ),
          englishMeaningConst,
        ],
      ),
    );
  }
}

